namespace SmallLang.Compiler.Generators;

[Generator(LanguageNames.CSharp)]
public sealed class ParserSourceGenerator : IIncrementalGenerator
{
    private static readonly DiagnosticDescriptor InvalidSpec = new(
        id: "SLANGGEN002",
        title: "Invalid SmallLang grammar specification",
        messageFormat: "{0}",
        category: "SmallLang.Parser",
        defaultSeverity: DiagnosticSeverity.Error,
        isEnabledByDefault: true);

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        var specs = context.AdditionalTextsProvider
            .Where(static text => Path.GetFileName(text.Path).Equals("smalllang.grammar", StringComparison.OrdinalIgnoreCase))
            .Select(static (text, cancellationToken) => GrammarSpec.Parse(
                text.Path,
                text.GetText(cancellationToken)?.ToString() ?? string.Empty));

        context.RegisterSourceOutput(specs, static (sourceProductionContext, spec) =>
        {
            if (spec.Error is not null)
            {
                sourceProductionContext.ReportDiagnostic(Diagnostic.Create(InvalidSpec, Location.None, spec.Error));
                return;
            }

            sourceProductionContext.AddSource(
                "GeneratedParser.g.cs",
                SourceText.From(ParserEmitter.Emit(spec), Encoding.UTF8));
        });
    }
}

internal sealed record GrammarSpec(
    string ParserType,
    string StartRule,
    ImmutableArray<GrammarRule> Rules,
    string? Error)
{
    private static readonly string[] RequiredRules =
    [
        "SourceFile",
        "NamespaceDeclaration",
        "ImportDeclaration",
        "StructDeclaration",
        "StructFieldDeclaration",
        "EnumDeclaration",
        "EnumVariantDeclaration",
        "TraitDeclaration",
        "TraitMethodDeclaration",
        "ImplDeclaration",
        "MethodDeclaration",
        "FunctionDeclaration",
        "GenericParameterClause",
        "FunctionSignature",
        "FunctionBody",
        "BlockFunctionBody",
        "MainBlock",
        "Statement",
        "BlockFunctionCallStatement",
        "EachStatement",
        "BindingStatement",
        "FieldAssignmentStatement",
        "ExpressionStatement",
        "StatementEnd",
        "RangeExpression",
        "Expression",
        "FlowExpression",
        "RangeOrLogicalExpression",
        "IfFlowTarget",
        "WhenFlowTarget",
        "FoldFlowTarget",
        "WhenExpression",
        "WhenArm",
        "WhenCondition",
        "SubjectWhenArm",
        "SubjectWhenCondition",
        "EnumPattern",
        "WhenArmBody",
        "BlockBody",
        "LogicalOrExpression",
        "LogicalAndExpression",
        "EqualityExpression",
        "ComparisonExpression",
        "AdditiveExpression",
        "MultiplicativeExpression",
        "UnaryExpression",
        "BoxExpression",
        "PrimaryExpression",
        "PostfixExpression",
        "AtomExpression",
        "StructLiteralExpression",
        "StructFieldInitializer",
        "CallExpression",
        "ArgumentList",
        "Path",
        "TypeName",
        "StringExpression",
        "NumberExpression",
        "NameExpression",
        "GuardLoopControlStatement",
        "ReturnStatement"
    ];

    public static GrammarSpec Parse(string path, string text)
    {
        var parserType = "SmallLang.Compiler.Parsing.Parser";
        var startRule = "SourceFile";
        var rules = ImmutableArray.CreateBuilder<GrammarRule>();
        var names = new HashSet<string>(StringComparer.Ordinal);

        var lines = text.Replace("\r\n", "\n").Replace('\r', '\n').Split('\n');
        for (var i = 0; i < lines.Length; i++)
        {
            var lineNumber = i + 1;
            var line = StripComment(lines[i]).Trim();
            if (line.Length == 0)
            {
                continue;
            }

            if (line.StartsWith("parser ", StringComparison.Ordinal))
            {
                parserType = line.Substring("parser ".Length).Trim();
                if (!IsQualifiedIdentifier(parserType))
                {
                    return Failure(path, lineNumber, $"invalid parser type '{parserType}'");
                }

                continue;
            }

            if (line.StartsWith("start ", StringComparison.Ordinal))
            {
                startRule = line.Substring("start ".Length).Trim();
                if (!IsIdentifier(startRule))
                {
                    return Failure(path, lineNumber, $"invalid start rule '{startRule}'");
                }

                continue;
            }

            if (line.StartsWith("rule ", StringComparison.Ordinal))
            {
                var parsed = ParseRule(line.Substring("rule ".Length), path, lineNumber);
                if (parsed.Error is not null)
                {
                    return parsed.Error;
                }

                if (!names.Add(parsed.Rule!.Name))
                {
                    return Failure(path, lineNumber, $"duplicate grammar rule '{parsed.Rule.Name}'");
                }

                rules.Add(parsed.Rule);
                continue;
            }

            return Failure(path, lineNumber, $"expected 'parser', 'start', or 'rule', got '{line}'");
        }

        if (startRule != "SourceFile")
        {
            return Failure(path, 0, "current parser generator requires 'start SourceFile'");
        }

        foreach (var required in RequiredRules)
        {
            if (!names.Contains(required))
            {
                return Failure(path, 0, $"grammar specification must declare rule '{required}'");
            }
        }

        return new GrammarSpec(parserType, startRule, rules.ToImmutable(), null);
    }

    private static GrammarParseResult ParseRule(string line, string path, int lineNumber)
    {
        var equal = line.IndexOf('=');
        if (equal < 0)
        {
            return GrammarParseResult.Fail(Failure(path, lineNumber, "rule must use '='"));
        }

        var name = line.Substring(0, equal).Trim();
        var production = line.Substring(equal + 1).Trim();
        if (!IsIdentifier(name))
        {
            return GrammarParseResult.Fail(Failure(path, lineNumber, $"invalid rule name '{name}'"));
        }

        if (production.Length == 0)
        {
            return GrammarParseResult.Fail(Failure(path, lineNumber, $"rule '{name}' has an empty production"));
        }

        return GrammarParseResult.Ok(new GrammarRule(name, production));
    }

    private static string StripComment(string line)
    {
        var comment = line.IndexOf('#');
        return comment >= 0 ? line.Substring(0, comment) : line;
    }

    private static bool IsQualifiedIdentifier(string value)
    {
        return value.Split('.').All(IsIdentifier);
    }

    private static bool IsIdentifier(string value)
    {
        if (value.Length == 0 || !(value[0] == '_' || char.IsLetter(value[0])))
        {
            return false;
        }

        for (var i = 1; i < value.Length; i++)
        {
            if (!(value[i] == '_' || char.IsLetterOrDigit(value[i])))
            {
                return false;
            }
        }

        return true;
    }

    private static GrammarSpec Failure(string path, int line, string message)
    {
        var prefix = line > 0 ? $"{path}:{line}" : path;
        return new GrammarSpec(
            "SmallLang.Compiler.Parsing.Parser",
            "SourceFile",
            ImmutableArray<GrammarRule>.Empty,
            $"{prefix}: {message}");
    }
}

internal sealed record GrammarRule(string Name, string Production);

internal sealed record GrammarParseResult(GrammarRule? Rule, GrammarSpec? Error)
{
    public static GrammarParseResult Ok(GrammarRule rule) => new(rule, null);

    public static GrammarParseResult Fail(GrammarSpec error) => new(null, error);
}

internal static class ParserEmitter
{
    public static string Emit(GrammarSpec spec)
    {
        var lastDot = spec.ParserType.LastIndexOf('.');
        var ns = lastDot >= 0 ? spec.ParserType.Substring(0, lastDot) : "SmallLang.Compiler.Parsing";
        var typeName = lastDot >= 0 ? spec.ParserType.Substring(lastDot + 1) : spec.ParserType;

        var builder = new StringBuilder();
        builder.AppendLine("// <auto-generated />");
        builder.AppendLine("#nullable enable");
        builder.AppendLine("using System;");
        builder.AppendLine("using System.Collections.Generic;");
        builder.AppendLine("using System.Globalization;");
        builder.AppendLine("using System.Linq;");
        builder.AppendLine("using SmallLang.Compiler.Diagnostics;");
        builder.AppendLine("using SmallLang.Compiler.Lexing;");
        builder.AppendLine("using SmallLang.Compiler.Syntax;");
        builder.AppendLine();
        builder.Append("namespace ").Append(ns).AppendLine(";");
        builder.AppendLine();
        builder.AppendLine("// Grammar source:");
        foreach (var rule in spec.Rules)
        {
            builder.Append("//   ")
                .Append(rule.Name)
                .Append(" = ")
                .AppendLine(rule.Production);
        }

        builder.AppendLine();
        builder.Append("internal sealed class ").Append(typeName).AppendLine("(IReadOnlyList<Token> tokens, bool isStandardLibrary = false)");
        builder.AppendLine("{");
        builder.AppendLine("    private int _index;");
        builder.AppendLine("    private IReadOnlyList<string> _namespacePath = [];");
        builder.AppendLine("    private readonly Dictionary<string, IReadOnlyList<string>> _imports = new(StringComparer.Ordinal);");
        builder.AppendLine("    private readonly HashSet<string> _activeAssociatedTypeNames = new(StringComparer.Ordinal);");
        builder.AppendLine("    private readonly HashSet<string> _activeGenericParameterNames = new(StringComparer.Ordinal);");
        builder.AppendLine("    private readonly List<StructDeclaration> _nestedStructs = [];");
        builder.AppendLine("    private readonly HashSet<string> _knownNestedTypeNames = new(StringComparer.Ordinal);");
        builder.AppendLine("    private readonly Stack<string> _activeTypeScopes = new();");
        builder.AppendLine();
        builder.AppendLine("    public SmallLangProgram Parse()");
        builder.AppendLine("    {");
        builder.AppendLine("        // SourceFile = NewLine* NamespaceDeclaration? ImportDeclaration* FunctionDeclaration* (MainBlock | Statement*) NewLine* End");
        builder.AppendLine("        var structs = new List<StructDeclaration>();");
        builder.AppendLine("        var enums = new List<EnumDeclaration>();");
        builder.AppendLine("        var traits = new List<TraitDeclaration>();");
        builder.AppendLine("        var functions = new List<FunctionDeclaration>();");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine("        if (CheckIdentifier(\"namespace\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            ParseNamespaceDeclaration();");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        while (CheckIdentifier(\"import\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            ParseImportDeclaration();");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        while (CheckDeclarationStart(\"struct\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            structs.Add(ParseStructDeclaration());");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine("        structs.AddRange(_nestedStructs);");
        builder.AppendLine();
        builder.AppendLine("        while (CheckDeclarationStart(\"enum\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            enums.Add(ParseEnumDeclaration());");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        while (CheckDeclarationStart(\"trait\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            traits.Add(ParseTraitDeclaration());");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        while (CheckIdentifier(\"impl\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            functions.AddRange(ParseImplDeclaration());");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        while (IsFunctionDeclarationStart())");
        builder.AppendLine("        {");
        builder.AppendLine("            functions.Add(ParseFunctionDeclaration(isLocal: false));");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var statements = CheckIdentifier(\"main\")");
        builder.AppendLine("            ? ParseMainBlock()");
        builder.AppendLine("            : ParseTopLevelStatements();");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine("        Expect(TokenKind.End);");
        builder.AppendLine("        var imports = _imports.Select(static pair => new ImportDeclaration(pair.Value, pair.Key)).ToArray();");
        builder.AppendLine("        return new SmallLangProgram(_namespacePath, imports, structs, enums, traits, functions, statements);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    public Expression ParseExpressionFragment()");
        builder.AppendLine("    {");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine("        var expression = ParseExpression();");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine("        Expect(TokenKind.End);");
        builder.AppendLine("        return expression;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private void ParseNamespaceDeclaration()");
        builder.AppendLine("    {");
        builder.AppendLine("        // NamespaceDeclaration = Identifier(\"namespace\") Path StatementEnd");
        builder.AppendLine("        ExpectIdentifier(\"namespace\");");
        builder.AppendLine("        var start = ExpectIdentifier();");
        builder.AppendLine("        _namespacePath = ParsePathAfterFirstIdentifier(start);");
        builder.AppendLine("        ExpectStatementEnd();");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private void ParseImportDeclaration()");
        builder.AppendLine("    {");
        builder.AppendLine("        // ImportDeclaration = Identifier(\"import\") Path (Identifier(\"as\") Identifier)? StatementEnd");
        builder.AppendLine("        var importToken = ExpectIdentifier(\"import\");");
        builder.AppendLine("        var start = ExpectIdentifier();");
        builder.AppendLine("        var path = ParsePathAfterFirstIdentifier(start);");
        builder.AppendLine("        var alias = path[path.Count - 1];");
        builder.AppendLine("        if (CheckIdentifier(\"as\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            ExpectIdentifier(\"as\");");
        builder.AppendLine("            alias = ExpectIdentifier().Text;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (_imports.ContainsKey(alias))");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(importToken, $\"import alias '{alias}' already exists\");");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        _imports.Add(alias, path);");
        builder.AppendLine("        ExpectStatementEnd();");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private StructDeclaration ParseStructDeclaration(string? declaringTypeName = null)");
        builder.AppendLine("    {");
        builder.AppendLine("        // StructDeclaration = Identifier(\"struct\") Identifier LeftBrace NewLine* StructFieldDeclaration* RightBrace");
        builder.AppendLine("        var isPublic = CheckIdentifier(\"public\");");
        builder.AppendLine("        if (isPublic) ExpectIdentifier(\"public\");");
        builder.AppendLine("        var keyword = ExpectIdentifier(\"struct\");");
        builder.AppendLine("        var name = ExpectIdentifier();");
        builder.AppendLine("        var declarationName = declaringTypeName is null ? QualifyTypeDeclarationName(name.Text) : declaringTypeName + \".\" + name.Text;");
        builder.AppendLine("        if (declaringTypeName is not null) _knownNestedTypeNames.Add(declarationName);");
        builder.AppendLine("        Expect(TokenKind.LeftBrace);");
        builder.AppendLine("        _activeTypeScopes.Push(declarationName);");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine("        var fields = new List<StructFieldDeclaration>();");
        builder.AppendLine("        while (!Check(TokenKind.RightBrace) && !Check(TokenKind.End))");
        builder.AppendLine("        {");
        builder.AppendLine("            if (CheckDeclarationStart(\"struct\"))");
        builder.AppendLine("            {");
        builder.AppendLine("                _nestedStructs.Add(ParseStructDeclaration(declarationName));");
        builder.AppendLine("                SkipNewLines();");
        builder.AppendLine("                continue;");
        builder.AppendLine("            }");
        builder.AppendLine("            var field = ExpectIdentifier();");
        builder.AppendLine("            Expect(TokenKind.Colon);");
        builder.AppendLine("            var fieldType = ParseTypeAnnotation();");
        builder.AppendLine("            var hadComma = Match(TokenKind.Comma, out _);");
        builder.AppendLine("            fields.Add(new StructFieldDeclaration(field.Text, fieldType, field.Line, field.Column));");
        builder.AppendLine("            if (!hadComma && !Check(TokenKind.NewLine) && !Check(TokenKind.RightBrace))");
        builder.AppendLine("            {");
        builder.AppendLine("                throw Error(Peek(), \"struct fields must be separated by a newline\");");
        builder.AppendLine("            }");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine("        Expect(TokenKind.RightBrace);");
        builder.AppendLine("        _activeTypeScopes.Pop();");
        builder.AppendLine("        return new StructDeclaration(declarationName, fields, keyword.Line, keyword.Column, string.Join('.', _namespacePath), isPublic, declaringTypeName);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private EnumDeclaration ParseEnumDeclaration()");
        builder.AppendLine("    {");
        builder.AppendLine("        // EnumDeclaration = Identifier(\"enum\") Identifier LeftBrace NewLine* EnumVariantDeclaration* RightBrace");
        builder.AppendLine("        var isPublic = CheckIdentifier(\"public\");");
        builder.AppendLine("        if (isPublic) ExpectIdentifier(\"public\");");
        builder.AppendLine("        var keyword = ExpectIdentifier(\"enum\");");
        builder.AppendLine("        var name = ExpectIdentifier();");
        builder.AppendLine("        var declarationName = QualifyTypeDeclarationName(name.Text);");
        builder.AppendLine("        Expect(TokenKind.LeftBrace);");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine("        var variants = new List<EnumVariantDeclaration>();");
        builder.AppendLine("        while (!Check(TokenKind.RightBrace) && !Check(TokenKind.End))");
        builder.AppendLine("        {");
        builder.AppendLine("            var variant = ExpectIdentifier();");
        builder.AppendLine("            string? payloadType = null;");
        builder.AppendLine("            if (Match(TokenKind.LeftParen, out _))");
        builder.AppendLine("            {");
        builder.AppendLine("                payloadType = ParseTypeAnnotation();");
        builder.AppendLine("                Expect(TokenKind.RightParen);");
        builder.AppendLine("            }");
        builder.AppendLine("            var hadComma = Match(TokenKind.Comma, out _);");
        builder.AppendLine("            variants.Add(new EnumVariantDeclaration(variant.Text, payloadType, variant.Line, variant.Column));");
        builder.AppendLine("            if (!hadComma && !Check(TokenKind.NewLine) && !Check(TokenKind.RightBrace))");
        builder.AppendLine("            {");
        builder.AppendLine("                throw Error(Peek(), \"enum variants must be separated by a comma or newline\");");
        builder.AppendLine("            }");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine("        Expect(TokenKind.RightBrace);");
        builder.AppendLine("        return new EnumDeclaration(declarationName, variants, keyword.Line, keyword.Column, string.Join('.', _namespacePath), isPublic);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private TraitDeclaration ParseTraitDeclaration()");
        builder.AppendLine("    {");
        builder.AppendLine("        var isPublic = CheckIdentifier(\"public\");");
        builder.AppendLine("        if (isPublic) ExpectIdentifier(\"public\");");
        builder.AppendLine("        var keyword = ExpectIdentifier(\"trait\");");
        builder.AppendLine("        var name = ExpectIdentifier();");
        builder.AppendLine("        var declarationName = QualifyTypeDeclarationName(name.Text);");
        builder.AppendLine("        Expect(TokenKind.LeftBrace);");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine("        var associatedTypes = new List<TraitAssociatedTypeDeclaration>();");
        builder.AppendLine("        var methods = new List<TraitMethodDeclaration>();");
        builder.AppendLine("        while (!Check(TokenKind.RightBrace) && !Check(TokenKind.End))");
        builder.AppendLine("        {");
        builder.AppendLine("            if (CheckIdentifier(\"type\"))");
        builder.AppendLine("            {");
        builder.AppendLine("                ExpectIdentifier(\"type\");");
        builder.AppendLine("                var associatedType = ExpectIdentifier();");
        builder.AppendLine("                if (!_activeAssociatedTypeNames.Add(associatedType.Text))");
        builder.AppendLine("                {");
        builder.AppendLine("                    throw Error(associatedType, $\"associated type '{associatedType.Text}' already exists\");");
        builder.AppendLine("                }");
        builder.AppendLine("                associatedTypes.Add(new TraitAssociatedTypeDeclaration(associatedType.Text, associatedType.Line, associatedType.Column));");
        builder.AppendLine("                ExpectStatementEnd();");
        builder.AppendLine("                SkipNewLines();");
        builder.AppendLine("                continue;");
        builder.AppendLine("            }");
        builder.AppendLine("            var method = ExpectIdentifier();");
        builder.AppendLine("            Expect(TokenKind.Colon);");
        builder.AppendLine("            var ownership = FunctionInputOwnership.Default;");
        builder.AppendLine("            if (CheckIdentifier(\"move\"))");
        builder.AppendLine("            {");
        builder.AppendLine("                ExpectIdentifier(\"move\");");
        builder.AppendLine("                ownership = FunctionInputOwnership.Move;");
        builder.AppendLine("            }");
        builder.AppendLine("            else if (CheckIdentifier(\"mut\"))");
        builder.AppendLine("            {");
        builder.AppendLine("                ExpectIdentifier(\"mut\");");
        builder.AppendLine("                ownership = FunctionInputOwnership.MutableBorrow;");
        builder.AppendLine("            }");
        builder.AppendLine("            ExpectIdentifier(\"self\");");
        builder.AppendLine("            Expect(TokenKind.Arrow);");
        builder.AppendLine("            var returnType = ParseTypeAnnotation();");
        builder.AppendLine("            ExpectStatementEnd();");
        builder.AppendLine("            methods.Add(new TraitMethodDeclaration(method.Text, ownership, returnType, method.Line, method.Column));");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine("        Expect(TokenKind.RightBrace);");
        builder.AppendLine("        _activeAssociatedTypeNames.Clear();");
        builder.AppendLine("        return new TraitDeclaration(declarationName, associatedTypes, methods, keyword.Line, keyword.Column, string.Join('.', _namespacePath), isPublic);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private IReadOnlyList<FunctionDeclaration> ParseImplDeclaration()");
        builder.AppendLine("    {");
        builder.AppendLine("        // ImplDeclaration = Identifier(\"impl\") TypeName LeftBrace NewLine* MethodDeclaration* RightBrace");
        builder.AppendLine("        ExpectIdentifier(\"impl\");");
        builder.AppendLine("        var firstTypeToken = ExpectIdentifier();");
        builder.AppendLine("        var firstType = ResolveTypePath(ParsePathAfterFirstIdentifier(firstTypeToken));");
        builder.AppendLine("        string? traitName = null;");
        builder.AppendLine("        var ownerType = firstType;");
        builder.AppendLine("        if (CheckIdentifier(\"for\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            ExpectIdentifier(\"for\");");
        builder.AppendLine("            traitName = firstType;");
        builder.AppendLine("            var ownerTypeToken = ExpectIdentifier();");
        builder.AppendLine("            ownerType = ResolveTypePath(ParsePathAfterFirstIdentifier(ownerTypeToken));");
        builder.AppendLine("        }");
        builder.AppendLine("        Expect(TokenKind.LeftBrace);");
        builder.AppendLine("        _activeTypeScopes.Push(ownerType);");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine("        var implAssociatedTypes = new Dictionary<string, string>(StringComparer.Ordinal);");
        builder.AppendLine("        while (CheckIdentifier(\"type\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            var typeKeyword = ExpectIdentifier(\"type\");");
        builder.AppendLine("            var associatedType = ExpectIdentifier();");
        builder.AppendLine("            Expect(TokenKind.Equal);");
        builder.AppendLine("            var concreteType = ParseTypeAnnotation();");
        builder.AppendLine("            if (!implAssociatedTypes.TryAdd(associatedType.Text, concreteType))");
        builder.AppendLine("            {");
        builder.AppendLine("                throw Error(typeKeyword, $\"associated type '{associatedType.Text}' is bound more than once\");");
        builder.AppendLine("            }");
        builder.AppendLine("            ExpectStatementEnd();");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine("        var methods = new List<FunctionDeclaration>();");
        builder.AppendLine("        while (!Check(TokenKind.RightBrace) && !Check(TokenKind.End))");
        builder.AppendLine("        {");
        builder.AppendLine("            if (!IsFunctionDeclarationStart())");
        builder.AppendLine("            {");
        builder.AppendLine("                throw Error(Peek(), \"expected method declaration\");");
        builder.AppendLine("            }");
        builder.AppendLine("            methods.Add(ParseFunctionDeclaration(isLocal: false, methodOwner: ownerType, traitName: traitName, implAssociatedTypes: implAssociatedTypes));");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine("        Expect(TokenKind.RightBrace);");
        builder.AppendLine("        _activeTypeScopes.Pop();");
        builder.AppendLine("        return methods;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private FunctionDeclaration ParseFunctionDeclaration(bool isLocal, string? methodOwner = null, string? traitName = null, IReadOnlyDictionary<string, string>? implAssociatedTypes = null)");
        builder.AppendLine("    {");
        builder.AppendLine("        // FunctionDeclaration = Path Identifier? Colon FunctionSignature (BlockFunctionBody | FunctionBody)");
        builder.AppendLine("        var isPublic = false;");
        builder.AppendLine("        if (CheckIdentifier(\"public\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            var publicToken = ExpectIdentifier(\"public\");");
        builder.AppendLine("            if (isLocal)");
        builder.AppendLine("            {");
        builder.AppendLine("                throw Error(publicToken, \"local functions cannot be public\");");
        builder.AppendLine("            }");
        builder.AppendLine("            isPublic = true;");
        builder.AppendLine("        }");
        builder.AppendLine("        var name = ExpectIdentifier();");
        builder.AppendLine("        var namePath = ParsePathAfterFirstIdentifier(name);");
        builder.AppendLine("        string? genericParameterName = null;");
        builder.AppendLine("        string? secondaryGenericParameterName = null;");
        builder.AppendLine("        string? genericTraitBound = null;");
        builder.AppendLine("        string? genericAssociatedTypeName = null;");
        builder.AppendLine("        string? genericAssociatedTypeConstraint = null;");
        builder.AppendLine("        var isValueGeneric = false;");
        builder.AppendLine("        if (Match(TokenKind.Less, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            if (methodOwner is not null)");
        builder.AppendLine("            {");
        builder.AppendLine("                throw Error(name, \"generic impl members are not implemented yet\");");
        builder.AppendLine("            }");
        builder.AppendLine("            genericParameterName = ExpectIdentifier().Text;");
        builder.AppendLine("            _activeGenericParameterNames.Add(genericParameterName);");
        builder.AppendLine("            if (Match(TokenKind.Colon, out _))");
        builder.AppendLine("            {");
        builder.AppendLine("                var traitBound = ExpectIdentifier();");
        builder.AppendLine("                genericTraitBound = ResolveTypePath(ParsePathAfterFirstIdentifier(traitBound));");
        builder.AppendLine("                isValueGeneric = genericTraitBound == \"Int\";");
        builder.AppendLine("                if (!isValueGeneric && Match(TokenKind.Less, out _))");
        builder.AppendLine("                {");
        builder.AppendLine("                    genericAssociatedTypeName = ExpectIdentifier().Text;");
        builder.AppendLine("                    Expect(TokenKind.Equal);");
        builder.AppendLine("                    genericAssociatedTypeConstraint = ParseTypeAnnotation();");
        builder.AppendLine("                    Expect(TokenKind.Greater);");
        builder.AppendLine("                }");
        builder.AppendLine("            }");
        builder.AppendLine("            if (Match(TokenKind.Comma, out _))");
        builder.AppendLine("            {");
        builder.AppendLine("                secondaryGenericParameterName = ExpectIdentifier().Text;");
        builder.AppendLine("                if (!_activeGenericParameterNames.Add(secondaryGenericParameterName))");
        builder.AppendLine("                {");
        builder.AppendLine("                    throw Error(name, $\"generic parameter '{secondaryGenericParameterName}' already exists\");");
        builder.AppendLine("                }");
        builder.AppendLine("            }");
        builder.AppendLine("            Expect(TokenKind.Greater);");
        builder.AppendLine("            if (CheckIdentifier(\"where\"))");
        builder.AppendLine("            {");
        builder.AppendLine("                ExpectIdentifier(\"where\");");
        builder.AppendLine("                var constrainedParameter = ExpectIdentifier();");
        builder.AppendLine("                if (constrainedParameter.Text != genericParameterName)");
        builder.AppendLine("                {");
        builder.AppendLine("                    throw Error(constrainedParameter, $\"where clause must constrain '{genericParameterName}'\");");
        builder.AppendLine("                }");
        builder.AppendLine("                Expect(TokenKind.Colon);");
        builder.AppendLine("                var traitBound = ExpectIdentifier();");
        builder.AppendLine("                genericTraitBound = ResolveTypePath(ParsePathAfterFirstIdentifier(traitBound));");
        builder.AppendLine("                if (Match(TokenKind.Less, out _))");
        builder.AppendLine("                {");
        builder.AppendLine("                    genericAssociatedTypeName = ExpectIdentifier().Text;");
        builder.AppendLine("                    Expect(TokenKind.Equal);");
        builder.AppendLine("                    genericAssociatedTypeConstraint = ParseTypeAnnotation();");
        builder.AppendLine("                    Expect(TokenKind.Greater);");
        builder.AppendLine("                }");
        builder.AppendLine("            }");
        builder.AppendLine("        }");
        builder.AppendLine("        if (methodOwner is not null && namePath.Count != 1)");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(name, \"method names cannot be path-qualified\");");
        builder.AppendLine("        }");
        builder.AppendLine("        var functionName = methodOwner is null");
        builder.AppendLine("            ? QualifyFunctionName(namePath, isLocal)");
        builder.AppendLine("            : traitName is null");
        builder.AppendLine("                ? methodOwner + \".\" + name.Text");
        builder.AppendLine("                : traitName + \".\" + methodOwner + \".\" + name.Text;");
        builder.AppendLine("        Token? inputName = null;");
        builder.AppendLine("        if (methodOwner is null && Check(TokenKind.Identifier) && CheckNext(TokenKind.Colon))");
        builder.AppendLine("        {");
        builder.AppendLine("            inputName = ExpectIdentifier();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        Expect(TokenKind.Colon);");
        builder.AppendLine("        var inputSignature = methodOwner is null");
        builder.AppendLine("            ? ParseOptionalInputSignature()");
        builder.AppendLine("            : ParseOptionalSelfSignature(methodOwner, out inputName);");
        builder.AppendLine("        var inputType = inputSignature.TypeName;");
        builder.AppendLine("        var inputOwnership = inputSignature.Ownership;");
        builder.AppendLine("        var hasValueGenericFixedArrayInput = isValueGeneric");
        builder.AppendLine("            && inputType == $\"[Int; {genericParameterName}]\";");
        builder.AppendLine("        if (inputName is not null && inputType is null)");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(inputName.Value, \"function input name requires an input type\");");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        Expect(TokenKind.Arrow);");
        builder.AppendLine("        var isAsync = false;");
        builder.AppendLine("        if (CheckIdentifier(\"async\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            ExpectIdentifier(\"async\");");
        builder.AppendLine("            isAsync = true;");
        builder.AppendLine("        }");
        builder.AppendLine("        var returnType = ParseTypeAnnotation();");
        builder.AppendLine("        if (methodOwner is not null && returnType == \"Self\")");
        builder.AppendLine("        {");
        builder.AppendLine("            returnType = methodOwner;");
        builder.AppendLine("        }");
        builder.AppendLine("        Token? blockInputName = null;");
        builder.AppendLine("        string? blockInputType = null;");
        builder.AppendLine("        var localFunctions = new List<FunctionDeclaration>();");
        builder.AppendLine("        Expression? body = null;");
        builder.AppendLine("        var blockBody = new List<Statement>();");
        builder.AppendLine("        var isIntrinsic = false;");
        builder.AppendLine("        if (CheckIdentifier(\"block\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            // BlockFunctionBody = Identifier(\"block\") Identifier Colon TypeAnnotation LeftBrace BlockBody RightBrace");
        builder.AppendLine("            ExpectIdentifier(\"block\");");
        builder.AppendLine("            blockInputName = ExpectIdentifier();");
        builder.AppendLine("            Expect(TokenKind.Colon);");
        builder.AppendLine("            blockInputType = ParseTypeAnnotation();");
        builder.AppendLine("            Expect(TokenKind.LeftBrace);");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("            if (returnType == \"Unit\")");
        builder.AppendLine("            {");
        builder.AppendLine("                while (!Check(TokenKind.RightBrace) && !Check(TokenKind.End))");
        builder.AppendLine("                {");
        builder.AppendLine("                    blockBody.Add(ParseStatement());");
        builder.AppendLine("                    SkipNewLines();");
        builder.AppendLine("                }");
        builder.AppendLine("            }");
        builder.AppendLine("            else");
        builder.AppendLine("            {");
        builder.AppendLine("                var parsedBody = ParseFunctionBlockBody();");
        builder.AppendLine("                blockBody.AddRange(parsedBody.Statements);");
        builder.AppendLine("                body = parsedBody.Value;");
        builder.AppendLine("            }");
        builder.AppendLine("            Expect(TokenKind.RightBrace);");
        builder.AppendLine("        }");
        builder.AppendLine("        else if (Match(TokenKind.Equal, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            ExpectIdentifier(\"intrinsic\");");
        builder.AppendLine("            isIntrinsic = true;");
        builder.AppendLine("        }");
        builder.AppendLine("        else if (Match(TokenKind.FatArrow, out _) || Match(TokenKind.Arrow, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            body = ParseExpression();");
        builder.AppendLine("        }");
        builder.AppendLine("        else");
        builder.AppendLine("        {");
        builder.AppendLine("            Expect(TokenKind.LeftBrace);");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("            while (IsFunctionDeclarationStart())");
        builder.AppendLine("            {");
        builder.AppendLine("                localFunctions.Add(ParseFunctionDeclaration(isLocal: true));");
        builder.AppendLine("                SkipNewLines();");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            var parsedBody = ParseFunctionBlockBody();");
        builder.AppendLine("            blockBody.AddRange(parsedBody.Statements);");
        builder.AppendLine("            body = parsedBody.Value;");
        builder.AppendLine("            Expect(TokenKind.RightBrace);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        _activeGenericParameterNames.Clear();");
        builder.AppendLine("        return new FunctionDeclaration(functionName, inputName?.Text, inputType, inputOwnership, returnType, blockInputName?.Text, blockInputType, localFunctions, body, blockBody, name.Line, name.Column, isIntrinsic, isStandardLibrary, traitName, genericParameterName, secondaryGenericParameterName, genericTraitBound, genericAssociatedTypeName, genericAssociatedTypeConstraint, implAssociatedTypes, isValueGeneric, hasValueGenericFixedArrayInput, string.Join('.', _namespacePath), isPublic, isAsync);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private FunctionInputSignature ParseOptionalSelfSignature(string ownerType, out Token? inputName)");
        builder.AppendLine("    {");
        builder.AppendLine("        inputName = null;");
        builder.AppendLine("        if (Check(TokenKind.Arrow))");
        builder.AppendLine("        {");
        builder.AppendLine("            return new FunctionInputSignature(null, FunctionInputOwnership.Default);");
        builder.AppendLine("        }");
        builder.AppendLine("        var ownership = FunctionInputOwnership.Default;");
        builder.AppendLine("        if (CheckIdentifier(\"move\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            ExpectIdentifier(\"move\");");
        builder.AppendLine("            ownership = FunctionInputOwnership.Move;");
        builder.AppendLine("        }");
        builder.AppendLine("        else if (CheckIdentifier(\"mut\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            ExpectIdentifier(\"mut\");");
        builder.AppendLine("            ownership = FunctionInputOwnership.MutableBorrow;");
        builder.AppendLine("        }");
        builder.AppendLine("        if (CheckIdentifier(\"self\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            inputName = ExpectIdentifier(\"self\");");
        builder.AppendLine("            return new FunctionInputSignature(ownerType, ownership);");
        builder.AppendLine("        }");
        builder.AppendLine("        if (ownership != FunctionInputOwnership.Default)");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(Peek(), \"move and mut receivers require 'self'\");");
        builder.AppendLine("        }");
        builder.AppendLine("        return new FunctionInputSignature(ParseTypeAnnotation(), FunctionInputOwnership.Default);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private BlockBody ParseFunctionBlockBody()");
        builder.AppendLine("    {");
        builder.AppendLine("        var statements = new List<Statement>();");
        builder.AppendLine("        while (!Check(TokenKind.RightBrace) && !Check(TokenKind.End))");
        builder.AppendLine("        {");
        builder.AppendLine("            if (!IsReturnStatement() && !IsGuardLoopControlStatement() && !CheckIdentifier(\"break\") && !CheckIdentifier(\"continue\") && TryParseBlockValue(out var value))");
        builder.AppendLine("            {");
        builder.AppendLine("                return new BlockBody(statements, value, value.Line, value.Column);");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            statements.Add(ParseStatement());");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return new BlockBody(statements, null, Peek().Line, Peek().Column);");
        builder.AppendLine("    }");

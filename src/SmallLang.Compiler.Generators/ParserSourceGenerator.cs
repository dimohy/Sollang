        builder.AppendLine("    private IReadOnlyList<Statement> ParseMainBlock()");
        builder.AppendLine("    {");
        builder.AppendLine("        // MainBlock = Identifier(\"main\") LeftBrace NewLine* Statement* RightBrace");
        builder.AppendLine("        var main = ExpectIdentifier(\"main\");");
        builder.AppendLine("        if (main.Text != \"main\")");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(main, \"expected 'main' block\");");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        Expect(TokenKind.LeftBrace);");
        builder.AppendLine("        var statements = new List<Statement>();");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine();
        builder.AppendLine("        while (!Check(TokenKind.RightBrace) && !Check(TokenKind.End))");
        builder.AppendLine("        {");
        builder.AppendLine("            statements.Add(ParseStatement());");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        Expect(TokenKind.RightBrace);");
        builder.AppendLine("        return statements;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private IReadOnlyList<Statement> ParseTopLevelStatements()");
        builder.AppendLine("    {");
        builder.AppendLine("        // Source files may omit main; remaining top-level statements become the main body.");
        builder.AppendLine("        var statements = new List<Statement>();");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine();
        builder.AppendLine("        while (!Check(TokenKind.End))");
        builder.AppendLine("        {");
        builder.AppendLine("            statements.Add(ParseStatement());");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return statements;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Statement ParseStatement()");
        builder.AppendLine("    {");
        builder.AppendLine("        // Statement = ReturnStatement | GuardLoopControlStatement | LoopControlStatement | BlockFunctionCallStatement | EachStatement | BindingStatement | ExpressionStatement");
        builder.AppendLine("        if (IsReturnStatement())");
        builder.AppendLine("        {");
        builder.AppendLine("            return ParseReturnStatement();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (IsGuardLoopControlStatement())");
        builder.AppendLine("        {");
        builder.AppendLine("            return ParseGuardLoopControlStatement();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (CheckIdentifier(\"break\") || CheckIdentifier(\"continue\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            return ParseLoopControlStatement();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (CheckIdentifier(\"each\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            return ParseEachStatement();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (TryParseLeadingIndexAssignment(out var leadingIndexAssignment))");
        builder.AppendLine("        {");
        builder.AppendLine("            return leadingIndexAssignment;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (TryParseBlockFunctionCallStatement(out var blockFunctionCallStatement))");
        builder.AppendLine("        {");
        builder.AppendLine("            return blockFunctionCallStatement;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Check(TokenKind.Identifier) && CheckNext(TokenKind.Equal))");
        builder.AppendLine("        {");
        builder.AppendLine("            return ParseBindingStatement();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return ParseExpressionStatement();");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Statement ParseLoopControlStatement()");
        builder.AppendLine("    {");
        builder.AppendLine("        // LoopControlStatement = (Identifier(\"break\") | Identifier(\"continue\")) StatementEnd");
        builder.AppendLine("        var keyword = Expect(TokenKind.Identifier);");
        builder.AppendLine("        ExpectStatementEnd();");
        builder.AppendLine("        var kind = keyword.Text == \"break\" ? LoopControlKind.Break : LoopControlKind.Continue;");
        builder.AppendLine("        return new LoopControlStatement(kind, keyword.Line, keyword.Column);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Statement ParseReturnStatement()");
        builder.AppendLine("    {");
        builder.AppendLine("        // ReturnStatement = Identifier(\"return\") StatementEnd | RangeOrLogicalExpression Arrow Identifier(\"return\") StatementEnd");
        builder.AppendLine("        if (CheckIdentifier(\"return\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            var keyword = ExpectIdentifier(\"return\");");
        builder.AppendLine("            ExpectStatementEnd();");
        builder.AppendLine("            return new ReturnStatement(null, keyword.Line, keyword.Column);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var value = ParseRangeOrLogicalExpression();");
        builder.AppendLine("        Expect(TokenKind.Arrow);");
        builder.AppendLine("        var returnKeyword = ExpectIdentifier(\"return\");");
        builder.AppendLine("        ExpectStatementEnd();");
        builder.AppendLine("        return new ReturnStatement(value, returnKeyword.Line, returnKeyword.Column);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private bool IsReturnStatement()");
        builder.AppendLine("    {");
        builder.AppendLine("        if (CheckIdentifier(\"return\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            return true;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var offset = 0;");
        builder.AppendLine("        var parentheses = 0;");
        builder.AppendLine("        var brackets = 0;");
        builder.AppendLine("        var braces = 0;");
        builder.AppendLine("        while (_index + offset < tokens.Count)");
        builder.AppendLine("        {");
        builder.AppendLine("            var kind = tokens[_index + offset].Kind;");
        builder.AppendLine("            if (parentheses == 0 && brackets == 0 && braces == 0");
        builder.AppendLine("                && kind is TokenKind.NewLine or TokenKind.RightBrace or TokenKind.End)");
        builder.AppendLine("            {");
        builder.AppendLine("                return offset >= 2");
        builder.AppendLine("                    && CheckAhead(offset - 2, TokenKind.Arrow)");
        builder.AppendLine("                    && CheckAheadIdentifier(offset - 1, \"return\");");
        builder.AppendLine("            }");
        builder.AppendLine("            switch (kind)");
        builder.AppendLine("            {");
        builder.AppendLine("                case TokenKind.LeftParen: parentheses++; break;");
        builder.AppendLine("                case TokenKind.RightParen: parentheses--; break;");
        builder.AppendLine("                case TokenKind.LeftBracket: brackets++; break;");
        builder.AppendLine("                case TokenKind.RightBracket: brackets--; break;");
        builder.AppendLine("                case TokenKind.LeftBrace: braces++; break;");
        builder.AppendLine("                case TokenKind.RightBrace when braces > 0: braces--; break;");
        builder.AppendLine("            }");
        builder.AppendLine("            offset++;");
        builder.AppendLine("        }");
        builder.AppendLine("        return false;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Statement ParseGuardLoopControlStatement()");
        builder.AppendLine("    {");
        builder.AppendLine("        // GuardLoopControlStatement = RangeOrLogicalExpression Arrow Identifier(\"if\") (Identifier(\"break\") | Identifier(\"continue\")) StatementEnd");
        builder.AppendLine("        var condition = ParseRangeOrLogicalExpression();");
        builder.AppendLine("        Expect(TokenKind.Arrow);");
        builder.AppendLine("        ExpectIdentifier(\"if\");");
        builder.AppendLine("        var keyword = Expect(TokenKind.Identifier);");
        builder.AppendLine("        ExpectStatementEnd();");
        builder.AppendLine("        var kind = keyword.Text == \"break\" ? LoopControlKind.Break : LoopControlKind.Continue;");
        builder.AppendLine("        return new GuardLoopControlStatement(condition, kind, keyword.Line, keyword.Column);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private bool IsGuardLoopControlStatement()");
        builder.AppendLine("    {");
        builder.AppendLine("        var offset = 0;");
        builder.AppendLine("        var parentheses = 0;");
        builder.AppendLine("        var brackets = 0;");
        builder.AppendLine("        var braces = 0;");
        builder.AppendLine("        while (_index + offset < tokens.Count)");
        builder.AppendLine("        {");
        builder.AppendLine("            var kind = tokens[_index + offset].Kind;");
        builder.AppendLine("            if (parentheses == 0 && brackets == 0 && braces == 0");
        builder.AppendLine("                && kind is TokenKind.NewLine or TokenKind.RightBrace or TokenKind.End)");
        builder.AppendLine("            {");
        builder.AppendLine("                return offset >= 3");
        builder.AppendLine("                    && CheckAhead(offset - 3, TokenKind.Arrow)");
        builder.AppendLine("                    && CheckAheadIdentifier(offset - 2, \"if\")");
        builder.AppendLine("                    && (CheckAheadIdentifier(offset - 1, \"break\")");
        builder.AppendLine("                        || CheckAheadIdentifier(offset - 1, \"continue\"));");
        builder.AppendLine("            }");
        builder.AppendLine("            switch (kind)");
        builder.AppendLine("            {");
        builder.AppendLine("                case TokenKind.LeftParen: parentheses++; break;");
        builder.AppendLine("                case TokenKind.RightParen: parentheses--; break;");
        builder.AppendLine("                case TokenKind.LeftBracket: brackets++; break;");
        builder.AppendLine("                case TokenKind.RightBracket: brackets--; break;");
        builder.AppendLine("                case TokenKind.LeftBrace: braces++; break;");
        builder.AppendLine("                case TokenKind.RightBrace when braces > 0: braces--; break;");
        builder.AppendLine("            }");
        builder.AppendLine("            offset++;");
        builder.AppendLine("        }");
        builder.AppendLine("        return false;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private bool TryParseLeadingIndexAssignment(out Statement statement)");
        builder.AppendLine("    {");
        builder.AppendLine("        // LeadingIndexAssignment = Identifier Bang? LeftBracket Expression RightBracket Equal Expression StatementEnd");
        builder.AppendLine("        statement = null!;");
        builder.AppendLine("        var saved = _index;");
        builder.AppendLine("        if (!Match(TokenKind.Identifier, out var name))");
        builder.AppendLine("        {");
        builder.AppendLine("            return false;");
        builder.AppendLine("        }");
        builder.AppendLine("        if (Match(TokenKind.Bang, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            name = name with { Text = name.Text + \"!\" };");
        builder.AppendLine("        }");
        builder.AppendLine("        if (!Match(TokenKind.LeftBracket, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            _index = saved;");
        builder.AppendLine("            return false;");
        builder.AppendLine("        }");
        builder.AppendLine("        var index = ParseExpression();");
        builder.AppendLine("        Expect(TokenKind.RightBracket);");
        builder.AppendLine("        if (!Match(TokenKind.Equal, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            _index = saved;");
        builder.AppendLine("            return false;");
        builder.AppendLine("        }");
        builder.AppendLine("        var value = ParseExpression();");
        builder.AppendLine("        ExpectStatementEnd();");
        builder.AppendLine("        statement = new IndexAssignmentStatement(name.Text, index, value, name.Line, name.Column);");
        builder.AppendLine("        return true;");
        builder.AppendLine("    }");

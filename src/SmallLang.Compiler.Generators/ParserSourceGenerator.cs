        builder.AppendLine("    private Expression ParseFlowExpression()");
        builder.AppendLine("    {");
        builder.AppendLine("        // FlowExpression = RangeOrLogicalExpression (Arrow (Path FlowTargetCall? | IfFlowTarget | WhenFlowTarget | FoldFlowTarget))*");
        builder.AppendLine("        var expression = ParseRangeOrLogicalExpression();");
        builder.AppendLine("        List<FlowTarget>? targets = null;");
        builder.AppendLine("        while (Match(TokenKind.Arrow, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            var target = ExpectIdentifier();");
        builder.AppendLine("            if (target.Text == \"if\")");
        builder.AppendLine("            {");
        builder.AppendLine("                expression = BuildFlowExpression(expression, targets);");
        builder.AppendLine("                targets = null;");
        builder.AppendLine("                var thenBody = ParseBlockBody();");
        builder.AppendLine("                BlockBody? elseBody = null;");
        builder.AppendLine("                if (CheckIdentifier(\"else\"))");
        builder.AppendLine("                {");
        builder.AppendLine("                    ExpectIdentifier(\"else\");");
        builder.AppendLine("                    elseBody = ParseBlockBody();");
        builder.AppendLine("                }");
        builder.AppendLine();
                builder.AppendLine("                expression = new IfExpression(expression, thenBody, elseBody, target.Line, target.Column);");
                builder.AppendLine("                continue;");
                builder.AppendLine("            }");
                builder.AppendLine();
                builder.AppendLine("            if (target.Text == \"when\")");
                builder.AppendLine("            {");
                builder.AppendLine("                expression = BuildFlowExpression(expression, targets);");
                builder.AppendLine("                targets = null;");
                builder.AppendLine("                expression = ParseSubjectWhenExpression(expression, target);");
                builder.AppendLine("                continue;");
                builder.AppendLine("            }");
                builder.AppendLine();
        builder.AppendLine("            if (target.Text == \"fold\")");
                builder.AppendLine("            {");
                builder.AppendLine("                expression = BuildFlowExpression(expression, targets);");
                builder.AppendLine("                targets = null;");
                builder.AppendLine("                expression = ParseFoldExpression(expression, target);");
        builder.AppendLine("                continue;");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            if (target.Text == \"each\" && (Check(TokenKind.LeftBrace) || (Check(TokenKind.Identifier) && CheckNext(TokenKind.LeftBrace))))");
        builder.AppendLine("            {");
        builder.AppendLine("                expression = BuildFlowExpression(expression, targets);");
        builder.AppendLine("                targets = null;");
        builder.AppendLine("                var itemName = Check(TokenKind.Identifier) ? ExpectIdentifier().Text : \"it\";");
        builder.AppendLine("                Expect(TokenKind.LeftBrace);");
        builder.AppendLine("                SkipNewLines();");
        builder.AppendLine("                var selector = ParseExpression();");
        builder.AppendLine("                Expression? dictionaryValueSelector = null;");
        builder.AppendLine("                if (Match(TokenKind.Colon, out _))");
        builder.AppendLine("                {");
        builder.AppendLine("                    dictionaryValueSelector = ParseExpression();");
        builder.AppendLine("                }");
        builder.AppendLine("                SkipNewLines();");
        builder.AppendLine("                Expect(TokenKind.RightBrace);");
        builder.AppendLine("                expression = new CompileTimeEachExpression(expression, itemName, selector, dictionaryValueSelector, target.Line, target.Column);");
        builder.AppendLine("                continue;");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            var path = ResolveImportedPath(ParsePathAfterFirstIdentifier(target));");
        builder.AppendLine("            string? typeArgument = null;");
        builder.AppendLine("            int? compileTimeValueArgument = null;");
        builder.AppendLine("            if (Match(TokenKind.Less, out _))");
        builder.AppendLine("            {");
        builder.AppendLine("                if (Check(TokenKind.Number))");
        builder.AppendLine("                {");
        builder.AppendLine("                    var valueArgument = Expect(TokenKind.Number);");
        builder.AppendLine("                    if (!int.TryParse(valueArgument.Text, out var parsedValueArgument) || parsedValueArgument < 0)");
        builder.AppendLine("                    {");
        builder.AppendLine("                        throw Error(valueArgument, \"compile-time value argument must be a non-negative Int literal\");");
        builder.AppendLine("                    }");
        builder.AppendLine("                    compileTimeValueArgument = parsedValueArgument;");
        builder.AppendLine("                }");
        builder.AppendLine("                else");
        builder.AppendLine("                {");
        builder.AppendLine("                    typeArgument = ParseTypeAnnotation();");
        builder.AppendLine("                }");
        builder.AppendLine("                Expect(TokenKind.Greater);");
        builder.AppendLine("            }");
        builder.AppendLine("            var usesCallSyntax = false;");
        builder.AppendLine("            if (Match(TokenKind.LeftParen, out _))");
                builder.AppendLine("            {");
                builder.AppendLine("                usesCallSyntax = true;");
                builder.AppendLine("                var arguments = ParseOptionalArgumentList();");
                builder.AppendLine("                Expect(TokenKind.RightParen);");
                builder.AppendLine("                targets ??= [];");
                builder.AppendLine("                targets.Add(new FlowTarget(path, arguments, usesCallSyntax, typeArgument, compileTimeValueArgument, target.Line, target.Column));");
                builder.AppendLine("                continue;");
                builder.AppendLine("            }");
                builder.AppendLine("            targets ??= [];");
                builder.AppendLine("            targets.Add(new FlowTarget(path, [], usesCallSyntax, typeArgument, compileTimeValueArgument, target.Line, target.Column));");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return BuildFlowExpression(expression, targets);");
        builder.AppendLine("    }");
        builder.AppendLine();

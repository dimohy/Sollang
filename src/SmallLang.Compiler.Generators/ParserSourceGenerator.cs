        builder.AppendLine("    private string ParseTypeAnnotation()");
        builder.AppendLine("    {");
        builder.AppendLine("        if (CheckIdentifier(\"box\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            ExpectIdentifier(\"box\");");
        builder.AppendLine("            var elementType = ExpectIdentifier();");
        builder.AppendLine("            return \"box \" + ResolveTypePath(ParsePathAfterFirstIdentifier(elementType));");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Match(TokenKind.LeftBracket, out var arrayStart))");
        builder.AppendLine("        {");
        builder.AppendLine("            var elementType = ExpectIdentifier();");
        builder.AppendLine("            var resolvedElementType = ResolveTypePath(ParsePathAfterFirstIdentifier(elementType));");
        builder.AppendLine("            if (Match(TokenKind.RightBracket, out _))");
        builder.AppendLine("            {");
        builder.AppendLine("                return $\"[{resolvedElementType}]\";");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            Expect(TokenKind.Semicolon);");
        builder.AppendLine("            if (Match(TokenKind.Identifier, out var fixedLength))");
        builder.AppendLine("            {");
        builder.AppendLine("                Expect(TokenKind.RightBracket);");
        builder.AppendLine("                return $\"[{resolvedElementType}; {fixedLength.Text}]\";");
        builder.AppendLine("            }");
        builder.AppendLine("            Expect(TokenKind.Tilde);");
        builder.AppendLine("            Expect(TokenKind.RightBracket);");
        builder.AppendLine("            return $\"[{resolvedElementType}; ~]\";");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Match(TokenKind.LeftBrace, out var dictStart))");
        builder.AppendLine("        {");
        builder.AppendLine("            var keyType = ExpectIdentifier();");
        builder.AppendLine("            Expect(TokenKind.Colon);");
        builder.AppendLine("            var valueType = ExpectIdentifier();");
        builder.AppendLine("            Expect(TokenKind.RightBrace);");
        builder.AppendLine("            return $\"{{{keyType.Text}: {valueType.Text}}}\";");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var typeName = ExpectIdentifier();");
        builder.AppendLine("        if ((typeName.Text == \"Option\" || typeName.Text == \"Result\") && Match(TokenKind.Less, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            var first = ParseTypeAnnotation();");
        builder.AppendLine("            if (typeName.Text == \"Option\")");
        builder.AppendLine("            {");
        builder.AppendLine("                Expect(TokenKind.Greater);");
        builder.AppendLine("                return $\"Option<{first}>\";");
        builder.AppendLine("            }");
        builder.AppendLine("            Expect(TokenKind.Comma);");
        builder.AppendLine("            var second = ParseTypeAnnotation();");
        builder.AppendLine("            Expect(TokenKind.Greater);");
        builder.AppendLine("            return $\"Result<{first}, {second}>\";");
        builder.AppendLine("        }");
        builder.AppendLine("        return ResolveTypePath(ParsePathAfterFirstIdentifier(typeName));");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private FunctionInputSignature ParseOptionalInputSignature()");
        builder.AppendLine("    {");
        builder.AppendLine("        // FunctionSignature = Arrow TypeAnnotation | InputTypeAnnotation Arrow TypeAnnotation");
        builder.AppendLine("        if (Check(TokenKind.Arrow))");
        builder.AppendLine("        {");
        builder.AppendLine("            return new FunctionInputSignature(null, FunctionInputOwnership.Default);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var ownership = FunctionInputOwnership.Default;");
        builder.AppendLine("        if (CheckIdentifier(\"move\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            ExpectIdentifier(\"move\");");
        builder.AppendLine("            ownership = FunctionInputOwnership.Move;");
        builder.AppendLine("        }");
        builder.AppendLine("        else if (CheckIdentifier(\"mut\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            ExpectIdentifier(\"mut\");");
        builder.AppendLine("            ownership = FunctionInputOwnership.MutableBorrow;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return new FunctionInputSignature(ParseTypeAnnotation(), ownership);");
        builder.AppendLine("    }");

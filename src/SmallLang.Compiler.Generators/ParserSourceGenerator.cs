        builder.AppendLine();
        builder.AppendLine("    private Expression ParseEqualityExpression()");
        builder.AppendLine("    {");
        builder.AppendLine("        // EqualityExpression = ComparisonExpression ((EqualEqual | BangEqual) ComparisonExpression)*");
        builder.AppendLine("        var expression = ParseComparisonExpression();");
        builder.AppendLine("        while (Match(TokenKind.EqualEqual, out var equal) || Match(TokenKind.BangEqual, out equal))");
        builder.AppendLine("        {");
        builder.AppendLine("            var op = equal.Kind == TokenKind.EqualEqual ? ComparisonOperator.Equal : ComparisonOperator.NotEqual;");
        builder.AppendLine("            var right = ParseComparisonExpression();");
        builder.AppendLine("            expression = new CompareExpression(expression, op, right, equal.Line, equal.Column);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return expression;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Expression ParseComparisonExpression()");
        builder.AppendLine("    {");
        builder.AppendLine("        // ComparisonExpression = AdditiveExpression ((Less | LessEqual | Greater | GreaterEqual) AdditiveExpression)*");
        builder.AppendLine("        var expression = ParseAdditiveExpression();");
        builder.AppendLine("        while (TryParseComparisonOperator(out var op, out var token))");
        builder.AppendLine("        {");
        builder.AppendLine("            var right = ParseAdditiveExpression();");
        builder.AppendLine("            expression = new CompareExpression(expression, op, right, token.Line, token.Column);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return expression;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private bool TryParseComparisonOperator(out ComparisonOperator op, out Token token)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (Match(TokenKind.Less, out token))");
        builder.AppendLine("        {");
        builder.AppendLine("            op = ComparisonOperator.Less;");
        builder.AppendLine("            return true;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Match(TokenKind.LessEqual, out token))");
        builder.AppendLine("        {");
        builder.AppendLine("            op = ComparisonOperator.LessOrEqual;");
        builder.AppendLine("            return true;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Match(TokenKind.Greater, out token))");
        builder.AppendLine("        {");
        builder.AppendLine("            op = ComparisonOperator.Greater;");
        builder.AppendLine("            return true;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Match(TokenKind.GreaterEqual, out token))");
        builder.AppendLine("        {");
        builder.AppendLine("            op = ComparisonOperator.GreaterOrEqual;");
        builder.AppendLine("            return true;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        op = default;");
        builder.AppendLine("        token = default;");
        builder.AppendLine("        return false;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Expression ParseAdditiveExpression()");
        builder.AppendLine("    {");
        builder.AppendLine("        // AdditiveExpression = MultiplicativeExpression ((Plus | Minus) MultiplicativeExpression)*");
        builder.AppendLine("        var expression = ParseMultiplicativeExpression();");
        builder.AppendLine("        while (Match(TokenKind.Plus, out var op) || Match(TokenKind.Minus, out op))");
        builder.AppendLine("        {");
        builder.AppendLine("            var right = ParseMultiplicativeExpression();");
        builder.AppendLine("            expression = op.Kind == TokenKind.Plus");
        builder.AppendLine("                ? new AddExpression(expression, right, op.Line, op.Column)");
        builder.AppendLine("                : new SubtractExpression(expression, right, op.Line, op.Column);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return expression;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Expression ParseMultiplicativeExpression()");
        builder.AppendLine("    {");
        builder.AppendLine("        // MultiplicativeExpression = UnaryExpression ((Star | Slash | Percent) UnaryExpression)*");
        builder.AppendLine("        var expression = ParseUnaryExpression();");
        builder.AppendLine("        while (Match(TokenKind.Star, out var op) || Match(TokenKind.Slash, out op) || Match(TokenKind.Percent, out op))");
        builder.AppendLine("        {");
        builder.AppendLine("            var right = ParseUnaryExpression();");
        builder.AppendLine("            expression = op.Kind switch");
        builder.AppendLine("            {");
        builder.AppendLine("                TokenKind.Star => new MultiplyExpression(expression, right, op.Line, op.Column),");
        builder.AppendLine("                TokenKind.Slash => new DivideExpression(expression, right, op.Line, op.Column),");
        builder.AppendLine("                TokenKind.Percent => new ModuloExpression(expression, right, op.Line, op.Column),");
        builder.AppendLine("                _ => throw Error(op, \"expected multiplicative operator\")");
        builder.AppendLine("            };");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return expression;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Expression ParseUnaryExpression()");
        builder.AppendLine("    {");
        builder.AppendLine("        // UnaryExpression = Identifier(\"not\") UnaryExpression | Minus UnaryExpression | PrimaryExpression");
        builder.AppendLine("        if (CheckIdentifier(\"not\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            var op = ExpectIdentifier(\"not\");");
        builder.AppendLine("            return new NotExpression(ParseUnaryExpression(), op.Line, op.Column);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (CheckIdentifier(\"box\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            var keyword = ExpectIdentifier(\"box\");");
        builder.AppendLine("            return new BoxExpression(ParseUnaryExpression(), keyword.Line, keyword.Column);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Match(TokenKind.Minus, out var minus))");
        builder.AppendLine("        {");
        builder.AppendLine("            return new NegateExpression(ParseUnaryExpression(), minus.Line, minus.Column);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return ParsePrimaryExpression();");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Expression ParsePrimaryExpression()");
        builder.AppendLine("    {");
        builder.AppendLine("        // PrimaryExpression = PostfixExpression");
        builder.AppendLine("        var expression = ParseAtomExpression();");
        builder.AppendLine("        while (Match(TokenKind.LeftBracket, out var bracket))");
        builder.AppendLine("        {");
        builder.AppendLine("            var index = ParseExpression();");
        builder.AppendLine("            Expect(TokenKind.RightBracket);");
        builder.AppendLine("            expression = new IndexExpression(expression, index, bracket.Line, bracket.Column);");
        builder.AppendLine("        }");
        builder.AppendLine("        while (Match(TokenKind.Dot, out var dot))");
        builder.AppendLine("        {");
        builder.AppendLine("            var field = ExpectIdentifier();");
        builder.AppendLine("            expression = new FieldAccessExpression(expression, field.Text, dot.Line, dot.Column);");
        builder.AppendLine("        }");
        builder.AppendLine("        while (Match(TokenKind.Question, out var question))");
        builder.AppendLine("        {");
        builder.AppendLine("            expression = new TryExpression(expression, question.Line, question.Column);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return expression;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Expression ParseAtomExpression()");
        builder.AppendLine("    {");
        builder.AppendLine("        // AtomExpression = WhenExpression | CallExpression | ArrayExpression | DictionaryExpression | StringExpression | NumberExpression | NameExpression | LeftParen Expression RightParen");
        builder.AppendLine("        if (CheckIdentifier(\"when\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            return ParseWhenExpression();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Match(TokenKind.LeftParen, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            var expression = ParseExpression();");
        builder.AppendLine("            Expect(TokenKind.RightParen);");
            builder.AppendLine("            return expression;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Check(TokenKind.LeftBracket))");
        builder.AppendLine("        {");
        builder.AppendLine("            return ParseArrayExpression();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Check(TokenKind.LeftBrace))");
        builder.AppendLine("        {");
        builder.AppendLine("            return ParseDictionaryExpression();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Match(TokenKind.String, out var stringToken))");
        builder.AppendLine("        {");
            builder.AppendLine("            return new StringExpression(StringLiteralParser.ParseStringSegments(stringToken), stringToken.Line, stringToken.Column);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Match(TokenKind.Number, out var numberToken))");
        builder.AppendLine("        {");
        builder.AppendLine("            return new NumberExpression(numberToken.Text, numberToken.Line, numberToken.Column);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Match(TokenKind.Identifier, out var identifier))");
        builder.AppendLine("        {");
        builder.AppendLine("            if (identifier.Text == \"map\")");
        builder.AppendLine("            {");
        builder.AppendLine("                return ParseMapExpression(identifier);");
        builder.AppendLine("            }");
        builder.AppendLine("            if (identifier.Text == \"true\" || identifier.Text == \"false\")");
        builder.AppendLine("            {");
        builder.AppendLine("                return new BoolExpression(identifier.Text == \"true\", identifier.Line, identifier.Column);");
        builder.AppendLine("            }");
        builder.AppendLine("            if ((identifier.Text == \"Option\" || identifier.Text == \"Result\") && Check(TokenKind.Less))");
        builder.AppendLine("            {");
        builder.AppendLine("                return ParseStandardGenericEnumConstructor(identifier);");
        builder.AppendLine("            }");
        builder.AppendLine("            if ((identifier.Text == \"Option\" || identifier.Text == \"Result\") && Check(TokenKind.LeftBracket))");
        builder.AppendLine("            {");
        builder.AppendLine("                throw Error(Peek(), \"generic type arguments use '<...>'; square brackets are reserved for arrays and indexing\");");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            return ParsePathStartingWith(identifier);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        throw Error(Peek(), \"expected expression\");");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Expression ParseMapExpression(Token keyword)");
        builder.AppendLine("    {");
        builder.AppendLine("        var modeToken = ExpectIdentifier();");
        builder.AppendLine("        var mode = modeToken.Text switch");
        builder.AppendLine("        {");
        builder.AppendLine("            \"read\" => MapAccessMode.Read,");
        builder.AppendLine("            \"write\" => MapAccessMode.Write,");
        builder.AppendLine("            _ => throw Error(modeToken, \"map expects 'read' or 'write'\")");
        builder.AppendLine("        };");
        builder.AppendLine("        var path = ParsePrimaryExpression();");
        builder.AppendLine("        Expression? offset = null;");
        builder.AppendLine("        Expression? length = null;");
        builder.AppendLine("        Expression? fileSize = null;");
        builder.AppendLine("        if (CheckIdentifier(\"at\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            ExpectIdentifier(\"at\");");
        builder.AppendLine("            offset = ParseAdditiveExpression();");
        builder.AppendLine("            ExpectIdentifier(\"for\");");
        builder.AppendLine("            length = ParseAdditiveExpression();");
        builder.AppendLine("        }");
        builder.AppendLine("        if (CheckIdentifier(\"size\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            if (mode != MapAccessMode.Write) throw Error(Peek(), \"only map write accepts 'size'\");");
        builder.AppendLine("            ExpectIdentifier(\"size\");");
        builder.AppendLine("            fileSize = ParseAdditiveExpression();");
        builder.AppendLine("        }");
        builder.AppendLine("        return new MapExpression(mode, path, offset, length, fileSize, keyword.Line, keyword.Column);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Expression ParseStandardGenericEnumConstructor(Token typeName)");
        builder.AppendLine("    {");
        builder.AppendLine("        Expect(TokenKind.Less);");
        builder.AppendLine("        var first = ParseTypeAnnotation();");
        builder.AppendLine("        string specialized;");
        builder.AppendLine("        if (typeName.Text == \"Option\")");
        builder.AppendLine("        {");
        builder.AppendLine("            Expect(TokenKind.Greater);");
        builder.AppendLine("            specialized = $\"Option<{first}>\";");
        builder.AppendLine("        }");
        builder.AppendLine("        else");
        builder.AppendLine("        {");
        builder.AppendLine("            Expect(TokenKind.Comma);");
        builder.AppendLine("            var second = ParseTypeAnnotation();");
        builder.AppendLine("            Expect(TokenKind.Greater);");
        builder.AppendLine("            specialized = $\"Result<{first}, {second}>\";");
        builder.AppendLine("        }");
        builder.AppendLine("        Expect(TokenKind.Dot);");
        builder.AppendLine("        var variant = ExpectIdentifier();");
        builder.AppendLine("        if (Match(TokenKind.LeftParen, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            var arguments = new List<Expression>();");
        builder.AppendLine("            if (!Check(TokenKind.RightParen)) ParseArgumentList(arguments);");
        builder.AppendLine("            Expect(TokenKind.RightParen);");
        builder.AppendLine("            return new CallExpression(new[] { specialized, variant.Text }, arguments, typeName.Line, typeName.Column);");
        builder.AppendLine("        }");
        builder.AppendLine("        return new FieldAccessExpression(new NameExpression(specialized, typeName.Line, typeName.Column), variant.Text, typeName.Line, typeName.Column);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Expression ParseStructLiteralExpression(string typeName, int line, int column)");
        builder.AppendLine("    {");
        builder.AppendLine("        // StructLiteralExpression = TypeName LeftBrace StructFieldInitializer* RightBrace");
        builder.AppendLine("        Expect(TokenKind.LeftBrace);");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine("        var fields = new List<StructFieldInitializer>();");
        builder.AppendLine("        while (!Check(TokenKind.RightBrace) && !Check(TokenKind.End))");
        builder.AppendLine("        {");
        builder.AppendLine("            var field = ExpectIdentifier();");
        builder.AppendLine("            Expect(TokenKind.Colon);");
        builder.AppendLine("            var value = ParseExpression();");
        builder.AppendLine("            fields.Add(new StructFieldInitializer(field.Text, value, field.Line, field.Column));");
        builder.AppendLine("            var hadComma = Match(TokenKind.Comma, out _);");
        builder.AppendLine("            if (!hadComma && !Check(TokenKind.NewLine) && !Check(TokenKind.RightBrace))");
        builder.AppendLine("            {");
        builder.AppendLine("                throw Error(Peek(), hadComma ? \"expected newline or '}' after struct field\" : \"expected ',' or newline after struct field\");");
        builder.AppendLine("            }");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine("        Expect(TokenKind.RightBrace);");
        builder.AppendLine("        return new StructLiteralExpression(typeName, fields, line, column);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Expression ParseArrayExpression()");
        builder.AppendLine("    {");
        builder.AppendLine("        // ArrayExpression = '[' NewLine* (TypedEmptyArray | ArrayItems) NewLine* ']'");
        builder.AppendLine("        var start = Expect(TokenKind.LeftBracket);");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine("        if (Check(TokenKind.Range))");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(Peek(), \"empty growable arrays now require an element type, for example '[Int; ~]'\");");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Check(TokenKind.RightBracket))");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(Peek(), \"empty growable array literals require an element type, for example '[Int; ~]'\");");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Check(TokenKind.Identifier) && CheckNext(TokenKind.Semicolon) && CheckAhead(2, TokenKind.Range))");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(Peek(), \"typed empty growable arrays use '~', for example '[Int; ~]' or '[Int; 1024~]'\");");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (LooksLikeTypedEmptyArrayLiteral())");
        builder.AppendLine("        {");
        builder.AppendLine("            var elementType = ExpectIdentifier();");
        builder.AppendLine("            var resolvedElementType = ResolveTypePath(ParsePathAfterFirstIdentifier(elementType));");
        builder.AppendLine("            Expect(TokenKind.Semicolon);");
        builder.AppendLine("            int? capacityHint = null;");
        builder.AppendLine("            if (Match(TokenKind.Number, out var capacity))");
        builder.AppendLine("            {");
        builder.AppendLine("                if (!int.TryParse(capacity.Text, out var parsedCapacity) || parsedCapacity < 0)");
        builder.AppendLine("                {");
        builder.AppendLine("                    throw Error(capacity, \"array capacity hint must be a non-negative integer literal\");");
        builder.AppendLine("                }");
        builder.AppendLine();
        builder.AppendLine("                capacityHint = parsedCapacity;");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            Expect(TokenKind.Tilde);");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("            Expect(TokenKind.RightBracket);");
        builder.AppendLine("            return new TypedEmptyArrayExpression(resolvedElementType, capacityHint, start.Line, start.Column);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Check(TokenKind.Identifier) && CheckNext(TokenKind.Semicolon) && CheckAheadAfterNewLines(2, TokenKind.LeftBrace))");
        builder.AppendLine("        {");
        builder.AppendLine("            var elementType = ExpectIdentifier();");
        builder.AppendLine("            Expect(TokenKind.Semicolon);");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("            var typedElements = new List<Expression>();");
        builder.AppendLine("            var typedIsDynamic = false;");
        builder.AppendLine("            while (!Check(TokenKind.RightBracket) && !Check(TokenKind.End))");
        builder.AppendLine("            {");
        builder.AppendLine("                typedElements.Add(ParseExpression());");
        builder.AppendLine("                SkipNewLines();");
        builder.AppendLine("                if (!Match(TokenKind.Comma, out _)) break;");
        builder.AppendLine("                SkipNewLines();");
        builder.AppendLine("                if (Match(TokenKind.Tilde, out _))");
        builder.AppendLine("                {");
        builder.AppendLine("                    typedIsDynamic = true;");
        builder.AppendLine("                    SkipNewLines();");
        builder.AppendLine("                    break;");
        builder.AppendLine("                }");
        builder.AppendLine("            }");
        builder.AppendLine("            Expect(TokenKind.RightBracket);");
        builder.AppendLine("            return new ArrayLiteralExpression(typedElements, typedIsDynamic, start.Line, start.Column, elementType.Text);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var first = ParseExpression();");
        builder.AppendLine("        if (Match(TokenKind.Semicolon, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            var count = Peek();");
        builder.AppendLine("            if (!Match(TokenKind.Number, out _) && !Match(TokenKind.Identifier, out _))");
        builder.AppendLine("            {");
        builder.AppendLine("                throw Error(count, \"array repeat count must be a non-negative integer literal or value parameter\");");
        builder.AppendLine("            }");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("            Expect(TokenKind.RightBracket);");
        builder.AppendLine("            var parsedCount = 0;");
        builder.AppendLine("            if (count.Kind == TokenKind.Number && (!int.TryParse(count.Text, out parsedCount) || parsedCount < 0))");
        builder.AppendLine("            {");
        builder.AppendLine("                throw Error(count, \"array repeat count must be a non-negative integer literal\");");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            return count.Kind == TokenKind.Number");
        builder.AppendLine("                ? new ArrayRepeatExpression(first, parsedCount, null, start.Line, start.Column)");
        builder.AppendLine("                : new ArrayRepeatExpression(first, null, count.Text, start.Line, start.Column);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var elements = new List<Expression>();");
        builder.AppendLine("        AddCompileTimeArrayElements(elements, first, start);");
        builder.AppendLine("        var isDynamic = false;");
        builder.AppendLine("        while (Match(TokenKind.Comma, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("            if (Match(TokenKind.Tilde, out _))");
        builder.AppendLine("            {");
        builder.AppendLine("                isDynamic = true;");
        builder.AppendLine("                SkipNewLines();");
        builder.AppendLine("                break;");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            if (Check(TokenKind.Range))");
        builder.AppendLine("            {");
        builder.AppendLine("                throw Error(Peek(), \"seeded growable arrays use '~', for example '[1, 2, ~]'\");");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            if (Check(TokenKind.RightBracket))");
        builder.AppendLine("            {");
        builder.AppendLine("                break;");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            AddCompileTimeArrayElements(elements, ParseExpression(), start);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine("        Expect(TokenKind.RightBracket);");
        builder.AppendLine("        return new ArrayLiteralExpression(elements, isDynamic, start.Line, start.Column);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private void AddCompileTimeArrayElements(List<Expression> elements, Expression expression, Token start)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (expression is RangeExpression range)");
        builder.AppendLine("        {");
        builder.AppendLine("            foreach (var item in ExpandCompileTimeRange(range, start))");
        builder.AppendLine("            {");
        builder.AppendLine("                elements.Add(new NumberExpression(item.ToString(CultureInfo.InvariantCulture), expression.Line, expression.Column));");
        builder.AppendLine("            }");
        builder.AppendLine("            return;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (expression is CompileTimeEachExpression each)");
        builder.AppendLine("        {");
        builder.AppendLine("            if (each.DictionaryValueSelector is not null)");
        builder.AppendLine("            {");
        builder.AppendLine("                throw Error(start, \"array compile-time each body must produce one value, not 'key: value'\");");
        builder.AppendLine("            }");
        builder.AppendLine("            if (each.Source is not RangeExpression sourceRange)");
        builder.AppendLine("            {");
        builder.AppendLine("                throw Error(start, \"compile-time each currently requires a constant integer range\");");
        builder.AppendLine("            }");
        builder.AppendLine("            foreach (var item in ExpandCompileTimeRange(sourceRange, start))");
        builder.AppendLine("            {");
        builder.AppendLine("                if (!TryEvaluateCompileTimeInt(each.Selector, each.ItemName, item, out var value))");
        builder.AppendLine("                {");
        builder.AppendLine("                    throw Error(start, \"compile-time each body must be a constant integer expression using its item\");");
        builder.AppendLine("                }");
        builder.AppendLine("                elements.Add(new NumberExpression(value.ToString(CultureInfo.InvariantCulture), each.Line, each.Column));");
        builder.AppendLine("            }");
        builder.AppendLine("            return;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        elements.Add(expression);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Expression ParseDictionaryExpression()");
        builder.AppendLine("    {");
        builder.AppendLine("        // DictionaryExpression = '{' (TypedEmptyDictionary | DictionaryEntry (',' DictionaryEntry)* ','?) '}'");
        builder.AppendLine("        var start = Expect(TokenKind.LeftBrace);");
        builder.AppendLine("        if (Check(TokenKind.RightBrace))");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(Peek(), \"empty dictionary literals require a type, for example '{Int: Int}'\");");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Check(TokenKind.Identifier) && CheckNext(TokenKind.Colon) && CheckAhead(2, TokenKind.Identifier) && (CheckAhead(3, TokenKind.RightBrace) || CheckAhead(3, TokenKind.Semicolon)))");
        builder.AppendLine("        {");
        builder.AppendLine("            var keyType = ExpectIdentifier();");
        builder.AppendLine("            Expect(TokenKind.Colon);");
        builder.AppendLine("            var valueType = ExpectIdentifier();");
        builder.AppendLine("            if (Check(TokenKind.RightBrace))");
        builder.AppendLine("            {");
        builder.AppendLine("                Expect(TokenKind.RightBrace);");
        builder.AppendLine("                return new TypedEmptyDictionaryExpression(keyType.Text, valueType.Text, null, start.Line, start.Column);");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            Expect(TokenKind.Semicolon);");
        builder.AppendLine("            int? capacityHint = null;");
        builder.AppendLine("            if (Check(TokenKind.Number) && CheckNext(TokenKind.Tilde))");
        builder.AppendLine("            {");
        builder.AppendLine("                var capacity = Expect(TokenKind.Number);");
        builder.AppendLine("                if (!int.TryParse(capacity.Text, out var parsedCapacity) || parsedCapacity < 0)");
        builder.AppendLine("                {");
        builder.AppendLine("                    throw Error(capacity, \"dictionary capacity hint must be a non-negative integer literal\");");
        builder.AppendLine("                }");
        builder.AppendLine();
        builder.AppendLine("                capacityHint = parsedCapacity;");
        builder.AppendLine("                Expect(TokenKind.Tilde);");
        builder.AppendLine("                Expect(TokenKind.RightBrace);");
        builder.AppendLine("                return new TypedEmptyDictionaryExpression(keyType.Text, valueType.Text, capacityHint, start.Line, start.Column);");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            var typedEntries = ParseDictionaryEntries();");
        builder.AppendLine("            Expect(TokenKind.RightBrace);");
        builder.AppendLine("            return new DictionaryLiteralExpression(typedEntries, start.Line, start.Column, keyType.Text, valueType.Text);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var entries = ParseDictionaryEntries();");
        builder.AppendLine("        Expect(TokenKind.RightBrace);");
        builder.AppendLine("        return new DictionaryLiteralExpression(entries, start.Line, start.Column);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private IReadOnlyList<DictionaryEntryExpression> ParseDictionaryEntries()");
        builder.AppendLine("    {");
        builder.AppendLine("        var entries = new List<DictionaryEntryExpression>();");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine("        while (!Check(TokenKind.RightBrace) && !Check(TokenKind.End))");
        builder.AppendLine("        {");
        builder.AppendLine("            var key = ParseExpression();");
        builder.AppendLine("            if (key is CompileTimeEachExpression each)");
        builder.AppendLine("            {");
        builder.AppendLine("                AddCompileTimeDictionaryEntries(entries, each);");
        builder.AppendLine("                SkipNewLines();");
        builder.AppendLine("                if (Match(TokenKind.Comma, out _)) SkipNewLines();");
        builder.AppendLine("                continue;");
        builder.AppendLine("            }");
        builder.AppendLine("            Expect(TokenKind.Colon);");
        builder.AppendLine("            var value = ParseExpression();");
        builder.AppendLine("            entries.Add(new DictionaryEntryExpression(key, value));");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("            if (!Match(TokenKind.Comma, out _))");
        builder.AppendLine("            {");
        builder.AppendLine("                break;");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("            if (Check(TokenKind.RightBrace))");
        builder.AppendLine("            {");
        builder.AppendLine("                break;");
        builder.AppendLine("            }");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return entries;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private void AddCompileTimeDictionaryEntries(List<DictionaryEntryExpression> entries, CompileTimeEachExpression each)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (each.Source is not RangeExpression sourceRange || each.DictionaryValueSelector is null)");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(Peek(), \"dictionary compile-time each requires a constant range and a 'key: value' body\");");
        builder.AppendLine("        }");
        builder.AppendLine("        foreach (var item in ExpandCompileTimeRange(sourceRange, Peek()))");
        builder.AppendLine("        {");
        builder.AppendLine("            if (!TryEvaluateCompileTimeInt(each.Selector, each.ItemName, item, out var key)");
        builder.AppendLine("                || !TryEvaluateCompileTimeInt(each.DictionaryValueSelector, each.ItemName, item, out var value))");
        builder.AppendLine("            {");
        builder.AppendLine("                throw Error(Peek(), \"dictionary compile-time each key and value must be constant integer expressions using its item\");");
        builder.AppendLine("            }");
        builder.AppendLine("            entries.Add(new DictionaryEntryExpression(");
        builder.AppendLine("                new NumberExpression(key.ToString(CultureInfo.InvariantCulture), each.Line, each.Column),");
        builder.AppendLine("                new NumberExpression(value.ToString(CultureInfo.InvariantCulture), each.Line, each.Column)));");
        builder.AppendLine("        }");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private IReadOnlyList<long> ExpandCompileTimeRange(RangeExpression range, Token token)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (!TryEvaluateCompileTimeInt(range.Start, null, 0, out var start)");
        builder.AppendLine("            || !TryEvaluateCompileTimeInt(range.End, null, 0, out var end))");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(token, \"compile-time range bounds must be constant integer expressions\");");
        builder.AppendLine("        }");
        builder.AppendLine("        if (end < start)");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(token, \"compile-time range end must be greater than or equal to its start\");");
        builder.AppendLine("        }");
        builder.AppendLine("        const long maximumElementCount = 100_000;");
        builder.AppendLine("        var count = checked(end - start + 1);");
        builder.AppendLine("        if (count > maximumElementCount)");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(token, $\"compile-time collection expansion exceeds the {maximumElementCount} element limit\");");
        builder.AppendLine("        }");
        builder.AppendLine("        var values = new long[checked((int)count)];");
        builder.AppendLine("        for (long index = 0; index < count; index++) values[index] = checked(start + index);");
        builder.AppendLine("        return values;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static bool TryEvaluateCompileTimeInt(Expression expression, string? itemName, long itemValue, out long value)");
        builder.AppendLine("    {");
        builder.AppendLine("        try");
        builder.AppendLine("        {");
        builder.AppendLine("            switch (expression)");
        builder.AppendLine("            {");
        builder.AppendLine("                case NumberExpression number when !number.Text.Contains('.') && !number.Text.Contains(\"e\", StringComparison.OrdinalIgnoreCase):");
        builder.AppendLine("                    return long.TryParse(number.Text, NumberStyles.Integer, CultureInfo.InvariantCulture, out value);");
        builder.AppendLine("                case NameExpression name when itemName is not null && name.Name == itemName:");
        builder.AppendLine("                    value = itemValue;");
        builder.AppendLine("                    return true;");
        builder.AppendLine("                case NegateExpression negate when TryEvaluateCompileTimeInt(negate.Value, itemName, itemValue, out var operand):");
        builder.AppendLine("                    value = checked(-operand);");
        builder.AppendLine("                    return true;");
        builder.AppendLine("                case AddExpression add:");
        builder.AppendLine("                    return TryEvaluateBinary(add.Left, add.Right, itemName, itemValue, static (left, right) => checked(left + right), out value);");
        builder.AppendLine("                case SubtractExpression subtract:");
        builder.AppendLine("                    return TryEvaluateBinary(subtract.Left, subtract.Right, itemName, itemValue, static (left, right) => checked(left - right), out value);");
        builder.AppendLine("                case MultiplyExpression multiply:");
        builder.AppendLine("                    return TryEvaluateBinary(multiply.Left, multiply.Right, itemName, itemValue, static (left, right) => checked(left * right), out value);");
        builder.AppendLine("                case DivideExpression divide:");
        builder.AppendLine("                    return TryEvaluateBinary(divide.Left, divide.Right, itemName, itemValue, static (left, right) => checked(left / right), out value);");
        builder.AppendLine("                case ModuloExpression modulo:");
        builder.AppendLine("                    return TryEvaluateBinary(modulo.Left, modulo.Right, itemName, itemValue, static (left, right) => checked(left % right), out value);");
        builder.AppendLine("            }");
        builder.AppendLine("        }");
        builder.AppendLine("        catch (ArithmeticException)");
        builder.AppendLine("        {");
        builder.AppendLine("        }");
        builder.AppendLine("        value = 0;");
        builder.AppendLine("        return false;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static bool TryEvaluateBinary(Expression leftExpression, Expression rightExpression, string? itemName, long itemValue, Func<long, long, long> operation, out long value)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (TryEvaluateCompileTimeInt(leftExpression, itemName, itemValue, out var left)");
        builder.AppendLine("            && TryEvaluateCompileTimeInt(rightExpression, itemName, itemValue, out var right))");
        builder.AppendLine("        {");
        builder.AppendLine("            value = operation(left, right);");
        builder.AppendLine("            return true;");
        builder.AppendLine("        }");
        builder.AppendLine("        value = 0;");
        builder.AppendLine("        return false;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private WhenExpression ParseWhenExpression()");
        builder.AppendLine("    {");
        builder.AppendLine("        // WhenExpression = Identifier(\"when\") LeftBrace NewLine* WhenArm+ Identifier(\"else\") WhenArmBody NewLine* RightBrace");
        builder.AppendLine("        var when = ExpectIdentifier(\"when\");");
        builder.AppendLine("        Expect(TokenKind.LeftBrace);");
        builder.AppendLine("        var arms = new List<WhenArm>();");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine();
        builder.AppendLine("        while (!CheckIdentifier(\"else\") && !Check(TokenKind.RightBrace) && !Check(TokenKind.End))");
        builder.AppendLine("        {");
        builder.AppendLine("            var condition = ParseWhenCondition();");
        builder.AppendLine("            var body = ParseWhenArmBody();");
        builder.AppendLine("            arms.Add(new WhenArm(condition, body, condition.Line, condition.Column));");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (arms.Count == 0)");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(when, \"when requires at least one condition arm\");");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        ExpectIdentifier(\"else\");");
        builder.AppendLine("        var elseBody = ParseWhenArmBody();");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine("        Expect(TokenKind.RightBrace);");
        builder.AppendLine("        return new WhenExpression(null, arms, elseBody, when.Line, when.Column);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Expression ParseSubjectWhenExpression(Expression subject, Token when)");
        builder.AppendLine("    {");
        builder.AppendLine("        // WhenFlowTarget = Identifier(\"when\") LeftBrace NewLine* SubjectWhenArm+ Identifier(\"else\") WhenArmBody NewLine* RightBrace");
        builder.AppendLine("        Expect(TokenKind.LeftBrace);");
        builder.AppendLine("        var arms = new List<WhenArm>();");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine();
        builder.AppendLine("        while (!CheckIdentifier(\"else\") && !Check(TokenKind.RightBrace) && !Check(TokenKind.End))");
        builder.AppendLine("        {");
        builder.AppendLine("            var condition = ParseSubjectWhenCondition();");
        builder.AppendLine("            var body = ParseWhenArmBody();");
        builder.AppendLine("            arms.Add(new WhenArm(condition, body, condition.Line, condition.Column));");
        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (arms.Count == 0)");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(when, \"when requires at least one condition arm\");");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        BlockBody? elseBody = null;");
        builder.AppendLine("        if (CheckIdentifier(\"else\"))");
        builder.AppendLine("        {");
        builder.AppendLine("            ExpectIdentifier(\"else\");");
        builder.AppendLine("            elseBody = ParseWhenArmBody();");
        builder.AppendLine("        }");
        builder.AppendLine("        SkipNewLines();");
        builder.AppendLine("        Expect(TokenKind.RightBrace);");
        builder.AppendLine("        var usesEnumPatterns = arms.Any(static arm => arm.Condition is EnumPatternExpression);");
        builder.AppendLine("        if (usesEnumPatterns)");
        builder.AppendLine("        {");
        builder.AppendLine("            if (arms.Any(static arm => arm.Condition is not EnumPatternExpression))");
        builder.AppendLine("            {");
        builder.AppendLine("                throw Error(when, \"enum patterns cannot be mixed with numeric subject conditions\");");
        builder.AppendLine("            }");
        builder.AppendLine("            return new EnumMatchExpression(subject, arms, elseBody, when.Line, when.Column);");
        builder.AppendLine("        }");
        builder.AppendLine("        if (elseBody is null)");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(when, \"numeric subject when requires an else arm\");");
        builder.AppendLine("        }");
        builder.AppendLine("        return new WhenExpression(subject, arms, elseBody, when.Line, when.Column);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Expression ParseWhenCondition()");
        builder.AppendLine("    {");
        builder.AppendLine("        // WhenCondition = SubjectWhenCondition | LogicalOrExpression");
        builder.AppendLine("        var saved = _index;");
        builder.AppendLine("        if (TryParseSubjectWhenCondition(out var subjectCondition))");
        builder.AppendLine("        {");
        builder.AppendLine("            return subjectCondition;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        _index = saved;");
        builder.AppendLine("        return ParseLogicalOrExpression();");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private BlockBody ParseWhenArmBody()");
        builder.AppendLine("    {");
        builder.AppendLine("        // WhenArmBody = FatArrow Expression | Arrow Expression | LeftBrace BlockBody RightBrace");
        builder.AppendLine("        if (Match(TokenKind.FatArrow, out var arrow) || Match(TokenKind.Arrow, out arrow))");
        builder.AppendLine("        {");
        builder.AppendLine("            return new BlockBody([], ParseExpression(), arrow.Line, arrow.Column);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return ParseBlockBody();");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Expression ParseSubjectWhenCondition()");
        builder.AppendLine("    {");
        builder.AppendLine("        // SubjectWhenCondition = (EqualEqual | BangEqual | Less | LessEqual | Greater | GreaterEqual) Expression | RangeExpression");
        builder.AppendLine("        if (Check(TokenKind.Identifier)");
        builder.AppendLine("            && (CheckNext(TokenKind.Dot)");
        builder.AppendLine("                || CheckNext(TokenKind.LeftParen)");
        builder.AppendLine("                || CheckNext(TokenKind.FatArrow)");
        builder.AppendLine("                || CheckNext(TokenKind.Arrow)");
        builder.AppendLine("                || CheckNext(TokenKind.LeftBrace)");
        builder.AppendLine("                || ((Peek().Text == \"Option\" || Peek().Text == \"Result\") && CheckNext(TokenKind.Less))))");
        builder.AppendLine("        {");
        builder.AppendLine("            return ParseEnumPattern();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (TryParseSubjectWhenCondition(out var condition))");
        builder.AppendLine("        {");
        builder.AppendLine("            return condition;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        throw Error(Peek(), \"expected subject comparison operator or range in value-flow when arm\");");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private EnumPatternExpression ParseEnumPattern()");
        builder.AppendLine("    {");
        builder.AppendLine("        // EnumPattern = qualified, payload shorthand, or payloadless shorthand before an arm body");
        builder.AppendLine("        var start = Peek();");
        builder.AppendLine("        var isQualified = CheckNext(TokenKind.Dot)");
        builder.AppendLine("            || ((start.Text == \"Option\" || start.Text == \"Result\") && CheckNext(TokenKind.Less));");
        builder.AppendLine("        var typeName = string.Empty;");
        builder.AppendLine("        Token variantName;");
        builder.AppendLine("        if (isQualified)");
        builder.AppendLine("        {");
        builder.AppendLine("            typeName = (start.Text == \"Option\" || start.Text == \"Result\") && CheckNext(TokenKind.Less)");
        builder.AppendLine("                ? ParseTypeAnnotation()");
        builder.AppendLine("                : ExpectIdentifier().Text;");
        builder.AppendLine("            Expect(TokenKind.Dot);");
        builder.AppendLine("            variantName = ExpectIdentifier();");
        builder.AppendLine("        }");
        builder.AppendLine("        else");
        builder.AppendLine("        {");
        builder.AppendLine("            variantName = ExpectIdentifier();");
        builder.AppendLine("        }");
        builder.AppendLine("        Token? binding = null;");
        builder.AppendLine("        if (Match(TokenKind.LeftParen, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            if (!Check(TokenKind.RightParen))");
        builder.AppendLine("            {");
        builder.AppendLine("                binding = ExpectIdentifier();");
        builder.AppendLine("            }");
        builder.AppendLine("            Expect(TokenKind.RightParen);");
        builder.AppendLine("        }");
        builder.AppendLine("        return new EnumPatternExpression(typeName, variantName.Text, binding?.Text, start.Line, start.Column);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private bool TryParseSubjectWhenCondition(out Expression condition)");
        builder.AppendLine("    {");
        builder.AppendLine("        var saved = _index;");
        builder.AppendLine("        if (TryParseSubjectWhenOperator(out var op, out var token))");
        builder.AppendLine("        {");
        builder.AppendLine("            condition = new SubjectCompareExpression(op, ParseLogicalOrExpression(), token.Line, token.Column);");
        builder.AppendLine("            return true;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var start = ParseLogicalOrExpression();");
        builder.AppendLine("        if (!Match(TokenKind.Range, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            _index = saved;");
        builder.AppendLine("            condition = null!;");
        builder.AppendLine("            return false;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var end = ParseLogicalOrExpression();");
        builder.AppendLine("        condition = new SubjectRangeExpression(start, end, start.Line, start.Column);");
        builder.AppendLine("        return true;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private bool TryParseSubjectWhenOperator(out ComparisonOperator op, out Token token)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (Match(TokenKind.EqualEqual, out token))");
        builder.AppendLine("        {");
        builder.AppendLine("            op = ComparisonOperator.Equal;");
        builder.AppendLine("            return true;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (Match(TokenKind.BangEqual, out token))");
        builder.AppendLine("        {");
        builder.AppendLine("            op = ComparisonOperator.NotEqual;");
        builder.AppendLine("            return true;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return TryParseComparisonOperator(out op, out token);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Expression ParsePathStartingWith(Token identifier)");
        builder.AppendLine("    {");
        builder.AppendLine("        // Path = Identifier (Dot Identifier)*");
        builder.AppendLine("        var path = ParsePathAfterFirstIdentifier(identifier);");
        builder.AppendLine("        if (LooksLikeTypeApplication())");
        builder.AppendLine("        {");
        builder.AppendLine("            Expect(TokenKind.Less);");
        builder.AppendLine("            var typeArgument = ParseTypeAnnotation();");
        builder.AppendLine("            Expect(TokenKind.Greater);");
        builder.AppendLine("            if (Check(TokenKind.LeftParen))");
        builder.AppendLine("            {");
        builder.AppendLine("                throw Error(Peek(), \"zero-input generic applications use property syntax without parentheses\");");
        builder.AppendLine("            }");
        builder.AppendLine("            return new TypeApplicationExpression(ResolveImportedPath(path), typeArgument, identifier.Line, identifier.Column);");
        builder.AppendLine("        }");
        builder.AppendLine("        if (Check(TokenKind.LeftBrace))");
        builder.AppendLine("        {");
        builder.AppendLine("            return ParseStructLiteralExpression(ResolveTypePath(path), identifier.Line, identifier.Column);");
        builder.AppendLine("        }");
        builder.AppendLine("        if (Match(TokenKind.LeftParen, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            return ParseCallExpression(identifier, ResolveImportedPath(path));");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        if (path.Count > 1 && _imports.ContainsKey(path[0]))");
        builder.AppendLine("        {");
        builder.AppendLine("            var resolved = ResolveImportedPath(path);");
        builder.AppendLine("            var ownerName = string.Join('.', resolved.Take(resolved.Count - 1));");
        builder.AppendLine("            return new FieldAccessExpression(");
        builder.AppendLine("                new NameExpression(ownerName, identifier.Line, identifier.Column),");
        builder.AppendLine("                resolved[^1],");
        builder.AppendLine("                identifier.Line,");
        builder.AppendLine("                identifier.Column);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var name = identifier.Text;");
        builder.AppendLine("        if (Match(TokenKind.Bang, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            name += \"!\";");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        Expression expression = new NameExpression(name, identifier.Line, identifier.Column);");
        builder.AppendLine("        for (var i = 1; i < path.Count; i++)");
        builder.AppendLine("        {");
        builder.AppendLine("            expression = new FieldAccessExpression(expression, path[i], identifier.Line, identifier.Column);");
        builder.AppendLine("        }");
        builder.AppendLine("        return expression;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private IReadOnlyList<string> ParsePathAfterFirstIdentifier(Token identifier)");
        builder.AppendLine("    {");
        builder.AppendLine("        // Path = Identifier (Dot Identifier)*");
        builder.AppendLine("        var path = new List<string> { identifier.Text };");
        builder.AppendLine("        while (Match(TokenKind.Dot, out _))");
        builder.AppendLine("        {");
        builder.AppendLine("            path.Add(ExpectIdentifier().Text);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return path;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private string QualifyFunctionName(IReadOnlyList<string> path, bool isLocal)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (isLocal || path.Count != 1 || _namespacePath.Count == 0)");
        builder.AppendLine("        {");
        builder.AppendLine("            return string.Join('.', path);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var qualified = new List<string>(_namespacePath.Count + 1);");
        builder.AppendLine("        foreach (var segment in _namespacePath)");
        builder.AppendLine("        {");
        builder.AppendLine("            qualified.Add(segment);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        qualified.Add(path[0]);");
        builder.AppendLine("        return string.Join('.', qualified);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private bool LooksLikeTypeApplication()");
        builder.AppendLine("    {");
        builder.AppendLine("        if (!Check(TokenKind.Less)) return false;");
        builder.AppendLine("        var savedIndex = _index;");
        builder.AppendLine("        try");
        builder.AppendLine("        {");
        builder.AppendLine("            Advance();");
        builder.AppendLine("            _ = ParseTypeAnnotation();");
        builder.AppendLine("            Expect(TokenKind.Greater);");
        builder.AppendLine("            return !Check(TokenKind.Identifier)");
        builder.AppendLine("                && !Check(TokenKind.Number)");
        builder.AppendLine("                && !Check(TokenKind.String)");
        builder.AppendLine("                && !Check(TokenKind.LeftBracket)");
        builder.AppendLine("                && !Check(TokenKind.LeftBrace);");
        builder.AppendLine("        }");
        builder.AppendLine("        catch (SmallLangException)");
        builder.AppendLine("        {");
        builder.AppendLine("            return false;");
        builder.AppendLine("        }");
        builder.AppendLine("        finally");
        builder.AppendLine("        {");
        builder.AppendLine("            _index = savedIndex;");
        builder.AppendLine("        }");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private bool LooksLikeTypedEmptyArrayLiteral()");
        builder.AppendLine("    {");
        builder.AppendLine("        var offset = 0;");
        builder.AppendLine("        if (!CheckAhead(offset, TokenKind.Identifier)) return false;");
        builder.AppendLine("        offset++;");
        builder.AppendLine("        while (CheckAhead(offset, TokenKind.Dot))");
        builder.AppendLine("        {");
        builder.AppendLine("            offset++;");
        builder.AppendLine("            if (!CheckAhead(offset, TokenKind.Identifier)) return false;");
        builder.AppendLine("            offset++;");
        builder.AppendLine("        }");
        builder.AppendLine("        if (!CheckAhead(offset, TokenKind.Semicolon)) return false;");
        builder.AppendLine("        offset++;");
        builder.AppendLine("        if (CheckAhead(offset, TokenKind.Tilde)) return true;");
        builder.AppendLine("        return CheckAhead(offset, TokenKind.Number) && CheckAhead(offset + 1, TokenKind.Tilde);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private string QualifyTypeDeclarationName(string name)");
        builder.AppendLine("    {");
        builder.AppendLine("        return _namespacePath.Count == 0 ? name : string.Join('.', _namespacePath) + \".\" + name;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private string ResolveTypePath(IReadOnlyList<string> path)");
        builder.AppendLine("    {");
        builder.AppendLine("        var resolved = ResolveImportedPath(path);");
        builder.AppendLine("        if (resolved.Count == 1 && !IsBuiltinTypeName(resolved[0]))");
        builder.AppendLine("        {");
        builder.AppendLine("            foreach (var scope in _activeTypeScopes)");
        builder.AppendLine("            {");
        builder.AppendLine("                var nestedName = scope + \".\" + resolved[0];");
        builder.AppendLine("                if (_knownNestedTypeNames.Contains(nestedName)) return nestedName;");
        builder.AppendLine("            }");
        builder.AppendLine("        }");
        builder.AppendLine("        if (resolved.Count != 1 || _namespacePath.Count == 0 || IsBuiltinTypeName(resolved[0]) || _activeAssociatedTypeNames.Contains(resolved[0]) || _activeGenericParameterNames.Contains(resolved[0]))");
        builder.AppendLine("        {");
        builder.AppendLine("            return string.Join('.', resolved);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return string.Join('.', _namespacePath) + \".\" + resolved[0];");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static bool IsBuiltinTypeName(string name)");
        builder.AppendLine("    {");
        builder.AppendLine("        return name is \"Unit\" or \"Text\" or \"Bool\" or \"Self\"");
        builder.AppendLine("            or \"Int\" or \"Long\" or \"Int8\" or \"Int16\" or \"Int32\" or \"Int64\"");
        builder.AppendLine("            or \"UInt8\" or \"UInt16\" or \"UInt32\" or \"UInt64\"");
        builder.AppendLine("            or \"Size\" or \"UIntSize\"");
        builder.AppendLine("            or \"CodePoint\"");
        builder.AppendLine("            or \"Arena\"");
        builder.AppendLine("            or \"Arguments\" or \"MappedBytes\" or \"MutableMappedBytes\"");
        builder.AppendLine("            or \"Float\" or \"Double\" or \"Float32\" or \"Float64\";");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private IReadOnlyList<string> ResolveImportedPath(IReadOnlyList<string> path)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (path.Count == 0 || !_imports.TryGetValue(path[0], out var importedPath))");
        builder.AppendLine("        {");
        builder.AppendLine("            return path;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var resolved = new List<string>(importedPath.Count + path.Count - 1);");
        builder.AppendLine("        foreach (var segment in importedPath)");
        builder.AppendLine("        {");
        builder.AppendLine("            resolved.Add(segment);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        for (var i = 1; i < path.Count; i++)");
        builder.AppendLine("        {");
        builder.AppendLine("            resolved.Add(path[i]);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return resolved;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private CallExpression ParseCallExpression(Token start, IReadOnlyList<string> path)");
        builder.AppendLine("    {");
        builder.AppendLine("        // CallExpression = Path LeftParen ArgumentList? RightParen");
        builder.AppendLine("        var arguments = ParseOptionalArgumentList();");
        builder.AppendLine("        Expect(TokenKind.RightParen);");
        builder.AppendLine("        return new CallExpression(path, arguments, start.Line, start.Column);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private IReadOnlyList<Expression> ParseOptionalArgumentList()");
        builder.AppendLine("    {");
        builder.AppendLine("        var arguments = new List<Expression>();");
        builder.AppendLine("        if (!Check(TokenKind.RightParen))");
        builder.AppendLine("        {");
        builder.AppendLine("            ParseArgumentList(arguments);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return arguments;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private void ParseArgumentList(List<Expression> arguments)");
        builder.AppendLine("    {");
        builder.AppendLine("        // ArgumentList = Expression (Comma Expression)*");
        builder.AppendLine("        do");
        builder.AppendLine("        {");
        builder.AppendLine("            arguments.Add(ParseExpression());");
        builder.AppendLine("        }");
        builder.AppendLine("        while (Match(TokenKind.Comma, out _));");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private void ExpectStatementEnd()");
        builder.AppendLine("    {");
        builder.AppendLine("        // StatementEnd = NewLine+ | lookahead(RightBrace) | lookahead(End)");
        builder.AppendLine("        if (Check(TokenKind.NewLine) || Check(TokenKind.RightBrace) || Check(TokenKind.End))");
        builder.AppendLine("        {");
        builder.AppendLine("            return;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        throw Error(Peek(), \"expected end of statement\");");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private void SkipNewLines()");
        builder.AppendLine("    {");
        builder.AppendLine("        while (Check(TokenKind.NewLine))");
        builder.AppendLine("        {");
        builder.AppendLine("            Advance();");
        builder.AppendLine("        }");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Token Expect(TokenKind kind)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (Match(kind, out var token))");
        builder.AppendLine("        {");
        builder.AppendLine("            return token;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        throw Error(Peek(), $\"expected {kind}\");");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private Token ExpectIdentifier(string? text = null)");
        builder.AppendLine("    {");
        builder.AppendLine("        var token = Expect(TokenKind.Identifier);");
        builder.AppendLine("        if (text is not null && token.Text != text)");
        builder.AppendLine("        {");
        builder.AppendLine("            throw Error(token, $\"expected '{text}'\");");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return token;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private bool Match(TokenKind kind, out Token token)");
        builder.AppendLine("    {");
        builder.AppendLine("        if (Check(kind))");
        builder.AppendLine("        {");
        builder.AppendLine("            token = Advance();");
        builder.AppendLine("            return true;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        token = default;");
        builder.AppendLine("        return false;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private bool Check(TokenKind kind)");
        builder.AppendLine("    {");
        builder.AppendLine("        return Peek().Kind == kind;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private bool CheckIdentifier(string text)");
        builder.AppendLine("    {");
        builder.AppendLine("        return Check(TokenKind.Identifier) && Peek().Text == text;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private bool CheckDeclarationStart(string keyword)");
        builder.AppendLine("    {");
        builder.AppendLine("        return CheckIdentifier(keyword)");
        builder.AppendLine("            || (CheckIdentifier(\"public\")");
        builder.AppendLine("                && _index + 1 < tokens.Count");
        builder.AppendLine("                && tokens[_index + 1].Kind == TokenKind.Identifier");
        builder.AppendLine("                && tokens[_index + 1].Text == keyword);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private bool IsFunctionDeclarationStart()");
        builder.AppendLine("    {");
        builder.AppendLine("        if (!Check(TokenKind.Identifier))");
        builder.AppendLine("        {");
        builder.AppendLine("            return false;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        var offset = CheckIdentifier(\"public\") ? 2 : 1;");
        builder.AppendLine("        while (CheckAhead(offset, TokenKind.Dot))");
        builder.AppendLine("        {");

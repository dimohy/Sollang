        builder.AppendLine("            SkipNewLines();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        Expect(TokenKind.RightBrace);");
        builder.AppendLine("        return new BlockBody(statements, null, start.Line, start.Column);");
        builder.AppendLine("    }");
        builder.AppendLine();
